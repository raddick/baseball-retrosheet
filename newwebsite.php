<font size="4"><p><b>Retrosheet's Game Collection</b></p></font>

As of our most recent release, Retrosheet has compiled game data as follows.
<ul>
<li>Complete play-by-play data for all AL and NL seasons from 1918 - 2022.<br>
<blockquote>Some of these accounts are deduced from newspaper stories and box scores. Some games are deduced for the following seasons: 1918 - 1959, 1962 - 1968, 1971 - 1973.</blockquote></li>

<li>Play-by-play data have also been released for most games for the 1912 - 1917 seasons.</li>
<li>Complete play-by-play, including some deduced accounts, have been released for the 1899 and 1900 National League seasons.</li>
<li>Box-score data have been compiled for all of the 1871 - 1875 and 1899 - 2022 seasons.</li>
<li>Negro League data have been compiled for the 1939 - 1949 seasons. In addition, Negro League data have been compiled for several hundred notable games played prior to 1939.</li>

</ul>

