<p><font size="4"><b>Welcome to Retrosheet</font></b><br>
Retrosheet was founded in 1989 for the purpose of computerizing  play-by-play accounts of as many pre-1984 major league games as possible. Retrosheet's work has expanded considerably from its original inception while retaining the same core focus: major-league games.<br><br>

Retrosheet's goal today is to provide as much information as possible about every baseball game played in major-league history. The results of our work can be explored through the menu items above. An overview of the game data we have available is summarized below. To return to Retrosheet's home page (i.e., this page), simply click the Retrosheet bat from any page.<br><br>

