<H2>Resumed Protested Game: 6/13/1943</H2>
<p>
In the top of the ninth after PHI tied the game at 3-3, Babe Dahlgren batted with the bases full and 2 out.
He deliberately stepped into a pitch. The rules stated he should have been called out but the ump (Reardon)
ruled no play and Dahlgren then hit a 3 run double. The Phils won 6-3.
Ford Frick reversed it but ordered the home 9th to count, contrary to the normal procedure of resuming
the game from the point of the protest. On August 6, the game restarted with the top of the 10th.
The Phils went out 1-2-3. The Giants then got 3 walks, a dropped pop foul, then a 4th walk to win 4-3.

