<h2><P>Donations</h2>


Retrosheet is an all-volunteer organization and the costs of daily operation
are largely borne by individuals who generously cover their own expenses for
postage, photocopying, etc. Retrosheet currently is in a strong financial position and
is using its funds for data acquisition and digitizing. If you wish to make a donation,
you can write a check paybable to Retrosheet (not to David Smith) and send it to:
<p>
Retrosheet<br>
20 Sunset Rd.<br>
Newark, DE 19711
<p>
Retrosheet is a non-profit corporation, incorporated in the State of Delaware in
1994.  In 2000 we received IRS approval as a 501(c)(3) charitable organization.
That status means that donations are fully tax-deductible to the extent permitted
by the tax code as it applies to individual's circumstances.
