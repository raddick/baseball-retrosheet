<p align="center"><b><font size="4">Umpire Data</font></b></p>

<p>The newest section of data now available on the website relates to umpires. We have complete yearly
rosters for all leagues starting with 1871. Many umpires are listed with biographical data (including 
burial location) and we continue to fill in gaps here. For most recent years we have a daily record for 
each arbiter and more of this data will appear in the future.
</p>
<p>This information is based on the groundbreaking research done by S.C. Thompson in the 1930s and 
continued more recently by Larry Gerlach. The current effort was coordinated by David Vincent, who collected the large majority of the biographical data and also recorded daily umpire assignments for thousands of games in the 19th century.  Bob Boynt

on has spent countless hours reading microfilm and gathering data for the 1945-1960 era.  Other people have contributed to this data collection as well, including Bob Boynton, Rich Carletti, 
John Schwartz and Joe Simenic. </p>
<p>The Sporting News used to keep index cards on active umpires listing biographical data as well as league assignments.  We were allowed access to these cards to gather information for our database.  Thank you to the fine people at TSN for allowing 

us access to the <A href="https://retrosheet.org/UmpireCards/">[cards]</A>.
</p>
<p>These cards include major and minor league umpires. File names are in the format Last-First. Names are those used on the cards and may not match what you are seeking. For example, Ziggy Sears is listed as Sears-John. Names do not include punctuati

on or spaces. For example, Jake O'Donnell is ODonnell-Jake and Clyde De Fate is DeFate-Clyde.</p>
 
<p>The card-based data collection stopped in the mid-1980s.</p>

