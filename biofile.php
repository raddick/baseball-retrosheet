<H2>Biographical Information</H2>

<p><b><u>Description of BioFile.txt</u></b>


<p>Retrosheet has released this large file that contains biographical information for every person who has played, managed, coached or umpired in the 
major leagues.  At this time, Negro League players, umpires, and coaches are not included unless they also participated in the American and/or National League.</p>


<p>This file replaces the previous file that had only ids, names, and debut dates (retroID.htm). Data here have been updated through the 2022 season.

<pre>
Each person has 33 items of information:
1.	Retrosheet ID
2.	Last Name
3.	First Name
4.	Use Name (How he was referred to in the news media)
5.	Birth Date
6.	Birth City
7.	Birth State
8.	Birth Country
9.	Debut Date as Player
10.	Date of Last Game as Player
11.	Debut Date as Manager
12.	Date of Last Game as Manager
13.	Debut Date as Coach
14.	Date of Last Game as Coach
15.	Debut Date as Umpire
16.	Date of Last Game as Umpire
17.	Death Date
18.	Death City
19.	Death State
20.	Death Country
21.	Bat
22.	Throw
23.	Height
24.	Weight
25.	Cemetery
26.	Cemetery City
27.	Cemetery State
28.	Cemetery Country
29.	Cemetery Note (A variety here such as exact grave location)
30.	Birth Name
31.	Name Change (Not used)
32.	Bat Change (Not used)
33.	HOF (Hall-of-Famers are denoted HOF; all others are blank)
</pre>
<p>Of course, many of these fields are blank for most people, reflecting either missing data (unknown birth date, height or weight, for example) or not relevant (for those who never coached, managed or umpired, for example). 

<p>The information in this file was compiled by many people over decades: David Vincent, David Smith and major contributions from the SABR Biographical Committee chaired by Bill Carle.  This file is very much a work in progress and new items routinely come to our attention and appear on the Retrosheet website on each individual's personal page.  

<p>It is our hope that this file will allow interested people to search, sort or otherwise explore interesting biographical data from the people who are the foundation of Major League history.

<p><b><u>Use of the file</u></b>

<p>The file format is "flat text" so that it can be read in a word processor or other text file reader.  The various fields for each person are separated by commas, so the file can be readily imported into a spreadsheet or database.  Most of the entries are contained within double quotes to attempt to ensure proper parsing, particularly of dates and numeric entries (e.g., height).
