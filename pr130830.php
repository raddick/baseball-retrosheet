<h3>08/30/1913 - New York at Philadelphia NL</h3>
<a href=https://www.retrosheet.org/boxesetc/1913/B08300PHI1913.htm>Boxscore</a>
<p>Umpire Brennan called the game in favor of the Giants with one out in the top of the 9th
inning and Philadelphia leading 8 to 6. When the first batter in the
9th struck out, John McGraw complained to the umpire that the fans
in centerfield were waving their straw hats and reflecting the sun
into the eyes of the batter. The umpire asked the Phillies to move
the fans, but the fans refused to move. After a lengthy consultation,
the game was called. - New York Times; 08/31/1913; p S1 (Giants)<br>
<br>
However, the forfeit was not permitted to stand:<br>
<br>
* New York Times, September 1, 1913: The Phillies appealed Brennan's 
decision.<br>
<br>
* New York Times, September 3, 1913: on September 2, NL President
Thomas Lynch reversed Brennan's decision 
declaring &quot;that the umpire plainly went beyond his authority in
declaring a forfeiture, for which he had neither the protection of
the regular playing rules nor of any special ground rule.&quot; The game
was awarded to Philadelphia, 8-6.<br>
<br>
* New York Times, September 7, 1913: The Giants appealed Lynch's
decision.<br>
<br>
* New York Times, September 16, 1913: The Board of Directors of the
National League overruled Lynch, deciding that the game will be
resumed on October 2, prior to that day's doubleheader, &quot;with the
same men on the field and the same status existing that existed on
the day ...&quot; As the Phillies were challenging the Giants for the 
pennant, this was thought to have Merkle-esque implications.<br>
<br>
* Come October 2, though, and the Giants have clinched. The game is 
picked up from the point where it was left off, but it's somewhat of 
an anticlimax.<br>
<br>
For those who want the details: Philadelphia 8, NY 6. Teams line 
up as they were when game ended on August 30th. Giants at bat in 
top of 9th, one out, none on. Murray grounds out. Meyers singles 
to right. Eddie Grant runs for Meyers. Larry McLean, batting 
for Snodgrass, forced Grant at second.<br>

