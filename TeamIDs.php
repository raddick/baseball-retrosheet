<h2>Franchise/Team IDs</h2>
The file linked below is comma delimited. There are 149 team-league combinations listed in the file.
<p>
The fields are:
<p>
Team Abbreviation<br> 
League<br> 
City<br>
Nickname <br>
First year of this combination<br>
Last year of this combination (most recent season for current franchises)
<p>
The league codes are:
<p>
NA = National Association<br> 
NL = National League<br>
AA = American Association<br>
UA = Union Association<br> 
PL = Players League<br>
AL = American League<br>
FL = Federal League<br>
<br>
