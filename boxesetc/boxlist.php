<h2>A List of Known Box Score Problems from 1914 to 1949</h2>
<P>
By Tom Ruane
</p>

<P>
What follows is a list of known problems in the box scores displayed
on our site from 1914 to 1949. These are cases where the data must be
wrong because of internal inconsistencies (for example, one team's
batting strikeout do not match the other team's pitching strikeouts).
Hopefully, the meaning will be obvious. For example, "PA anom" means
that there is a plate appearance anomaly for the team in question and
it is following by the number of plate appearances for each of the
team's nine lineup slots. The statistical mismatches show cases where
one team's batting statistics do not match the opposing team pitching
statistics. The batting team is show along with the two stats.
</p>

