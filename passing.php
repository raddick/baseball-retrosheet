Stewart, gave no signal. Dave May, running at first, hesitated, then started 
back to first base. Buford passed him and May was called out while Buford 
circled the bases. A five minute argument followed. Earl Weaver argued about 
Stewart's delayed home run call. Buford was credited with a run scoring single, 
and May with a run scored, even though those events never actually happened on 
the field. Home plate umpire Red Flaherty allegedly called May out, which was 
why he stopped running. The hit came off Ray Jarvis, who was ahead 6-0 at the 
