and third long enough for Sanchez to reach third. As Orioles catcher Caleb Joseph ran Stanton back toward
third, Stanton ran past the bag down the line a bit without being tagged. Joseph then tagged Sanchez who
was standing on third and went on to tag Stanton who had turned into foul territory. Stanton was ruled out and
Sanchez safe. Orioles manager Buck Showalter came out questioning the ruling saying that Sanchez should also
be out since he had passed Stanton once Stanton ran behind the base. The umpires huddled and did not change
the call. Later they admitted that they were wrong. After the similar play on 7/1/2015, which is described
above, rule 5.09(b)(9) was put in place saying the runner who reached third was out for passing the runner who was
on third and that runner, Stanton in this case, was entitled to third if he could get back to the bag before
being tagged out. This should have been an inning ending double play. Showalter protested the game, but since
the Orioles won 7-3 in 14 innings, the protest was withdrawn.

