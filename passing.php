<P><a href="https://www.retrosheet.org/boxesetc/1915/B09180BOS1915.htm">9/18/1915</A>
 - In the top of the twelfth inning of a scoreless game in
Boston, Ty Cobb led off with a double into the overflow crowd standing in right field. 
Bobby Veach bunted to move Cobb but Red Sox hurler Ernie Shore threw high to 
first and Veach was safe. Sam Crawford was walked intentionally to load the 
bases with none out. Marty Kavanagh forced Cobb at home. With one out and the 
bases still loaded, Ralph Young hit into another force out at home. 
However, Kavanagh, running from first on the play, rounded second and passed 
Crawford for the third out of the inning.</P>

<P><a href="https://www.retrosheet.org/boxesetc/1919/B04240CIN1919.htm">4/24/1919</A>
 - In the second game of the season at Redland Field in 
Cincinnati, with Cliff Heathcote on first with one out, Rogers Hornsby drove 
the ball to right center that might have gone for a triple or homer. However, 
Reds second sacker Morrie Rath faked Heathcote into believing that Rath had 
caught the ball. Heathcote returned towards first base while Hornsby was digging 
for second. Hornsby was called out for passing Heathcote.</P>

<P><a href="https://www.retrosheet.org/boxesetc/1920/B09120CHA1920.htm">9/12/1920</A> -
In the top of the fourth inning of a game in Chicago, 
Washington's Frank Ellerbe was on first base with two outs. Patsy Gharrity hit 
the ball into the left field bleachers for an apparent two-run homer. When 
Ellerbe heard the fans in those seats cheering he thought Joe Jackson had caught 
the ball which would have ended the inning. After rounding third base, Ellerbe 
turned and went to his shortstop position. Meanwhile Gharrity trotted around the 
bases. When he rounded third base, he was called out for passing Ellerbe. Both 
umpires, Bill Dinneen and Ollie Chill, made the call. Washington argued that 
since the ball was out of play it did not make any difference that Gharrity passed 
Ellerbe. The headline in the next days' <I>New York Times</I> read: "Gharrity's 
Homer Retires His Side." This event had no effect on the game as the Senators 
beat the White Sox, 5-0.</P>

<P><a href="https://www.retrosheet.org/boxesetc/1921/B06060CHN1921.htm">6/6/1921</a>
- In the bottom of the second, the Cubs had runners on first 
and second with no outs when Tom Daly hit a fly to right. Dodger Tommy Griffith 
lost the ball in the sun. John Kelleher, the runner on first, thought the ball 
would be caught and returned to the bag only to have Daly pass him.</P>

<P><a href="https://www.retrosheet.org/boxesetc/1924/B06150SLA1924.htm">6/15/1924</a>
- In the top of the eighth against the Browns, the A's had Bing Miller on third and
Chick Galloway on first with one out. Pinch hitter Sammy Hale hit a short fly to
right, but Jack Tobin dropped the ball allowing Miller to score. Galloway was
heading back to first thinking the ball would be caught when Hale passed him
after rounding first and was called out.</P>


<P><a href="https://www.retrosheet.org/boxesetc/1926/B08151BRO1926.htm">8/15/1926</a>
- In one of the strangest plays in major league history, the Dodgers loaded the bases with one out in the bottom of the 7th inning of game one versus the Braves. Babe Herman hit a flyball off the Ebbets Field right field screen. Hank DeBerry easily scored from third base. Dazzy Vance, the runner at second base, held up to see if the ball would be caught and then proceeded around third base and headed for home. Chick Fewster, the runner at first base, also held up before beginning his journey around the bases. Babe Herman, not paying close attention to what was transpiring in front of him, rounded second base and headed for third. Brooklyn's third base coach, seeing Herman close on the heels of Fewster both on their way to third base, yelled "go back, go back" in an attempt to get Babe to return to second base. However, for some strange reason, Vance thought those words were meant for him, so he returned to third base! Fewster saw Vance returning to third base so he slowed his approach. Herman, though, never slowed and slid "safely" into third. Confusion reigned at Ebbets Field since nobody had ever seen three baserunners on the same base before. Boston infielders tagged all three Dodgers presuming that two of them would have to be out. After huddling, the umpires ruled that Herman was out for passing Fewster (Herman had slid into third base moments before Fewster had arrived), and Fewster was out when he was tagged since Vance was the rightful occupant of third base. So receiving vital assists from bad baserunning in front of him and an ineffective third base coach, Babe Herman had just doubled into a double-play.

<P><a href="https://www.retrosheet.org/boxesetc/1926/B09220PHI1926.htm">9/22/1926</a>
- In the top of the seventh inning with one out, Pie Traynor hit a Texas Leaguer to
left with the bases loaded. The runner on second, Paul Waner, paused and George
Grantham passed Waner on the base line and was called out. Waner then scored, so
two runs scored on the play.

<P><a href="https://www.retrosheet.org/boxesetc/1930/B05301BRO1930.htm">5/30/1930</A> - 
In the first game of a Memorial Day doubleheader, Dodger 
Del Bissonette batted against Claude Willoughby of the Phillies in the bottom of 
the fourth inning with runners on first and second. As Bissonette's fly ball 
neared the fence, which it cleared for what should have been a home run, Babe Herman,
fearing a catch, stopped. Bissonette didn't see 
Herman and passed him. He was called out by umpire Lou Jorda and was credited 
with a 2-RBI single.</P>

<P><a href="https://www.retrosheet.org/boxesetc/1930/B09150BRO1930.htm">9/15/1930</A> - 
In the bottom of the first with runners on 1b and 2b, 
Dodger Glenn Wright hit the ball to right-center. It looked like it might be 
caught so Babe Herman slowed up as he neared 2b. The ball bounced over the fence 
for a home run but Wright had his head down as he ran between 1b and 2b. 
Consequently, he passed Herman and was called out. He lost a homer but still 
knocked in 2 runs.</P>

<P><a href="https://www.retrosheet.org/boxesetc/1931/B04260WS11931.htm">4/26/1931</A> - 
In the first inning with Lyn Lary on first and two out, 
Lou Gehrig hit a home run into the centerfield bleachers at Griffith Stadium. 
However, the ball caromed back to the centerfielder. Lary returned to the 
dugout, evidently thinking the ball was caught. Gehrig was declared out for 
passing the runner and lost the homer. He ended the year tied for the lead in 
homers with Babe Ruth.</P>

<P><a href="https://www.retrosheet.org/boxesetc/1933/B08250DET1933.htm">8/25/1933</A> - 
In the bottom of the third with one out, Pete Fox was hit by a pitch and Charlie Gehringer's infield single moved him to second. Gee Walker hit a ball off the right field wall. Gehringer realized it would not be caught and ran with his head down and passed Fox who was waiting near the bag to see if the ball would be caught. Gehringer was called out immediately for passing Fox, and Fox got a late start and was thrown out at the plate, so Walker managed to double into a double play.

<P><a href="https://www.retrosheet.org/boxesetc/1936/B07042SLA1936.htm">7/4/1936</A> - 
The Browns had the bases loaded with one out in the bottom of the 2nd inning of game two versus Detroit. Moose Solters hit a long drive that Tigers left fielder Goose Goslin lept for at the wall. Goslin came down with the ball holding it aloft for all to see. Al (Tommy) Thomas, the runner at third base, easily scored. Lyn Lary, the runner at second base, and Harlond Clift, the runner at first base, returned to their respective bases. However, the umpires ruled that Goslin had caught the ball after it rebounded off the wall so it was not a legal catch. Goslin threw the ball to Tigers third baseman Marv Owen who touched third base. After a short delay, Owen then threw the ball to Charlie Gehringer who stepped on second base. Tigers right fielder Gee Walker then grabbed the ball and ran over and tagged both Clift and Solters who were both standing on first base. The umpires huddled and then ruled an inning-ending double-play: Lary was forced out when Owen touched third base and Solters was declared out for passing Clift when he rounded first base as Clift was returning to first base. Thomas' run counted since the "passed runner" scenario removes the force and Solters was deemed to have passed Clift after Owen touched third base, so the third out of the inning was not a force play, which would have nullified the run.

<P><a href="https://www.retrosheet.org/boxesetc/1939/B07250WS11939.htm">7/25/1939</A> - 
Senators had runners on first and second base with one out in the bottom of the 7th inning versus Detroit. Jimmy Bloodworth hit a deep flyball to left field that Earl Averill dropped for an error. Taffy Wright was able to score from second base. Cecil Travis, the runner on first base, held up between first and second to see if the ball would be caught. Bloodworth, who sprinted into second base, was declared out for passing Travis.

<P><a href="https://www.retrosheet.org/boxesetc/1939/B08162BSN1939.htm">8/16/1939</A> - 
In the top of the seventh of the second game against the Braves, the Phillies needed a ruling on a passing a runner play to preserve a sacrifice fly. With one out, Morrie Arnovich on 3rd base and Gus Suhr on 1st base, Pinky May hit a flyball to right field. The Boston RF Deb Garms muffed the ball, but threw to 2nd to ostensibly force out Suhr (with Arnovich scoring from 3rd). However, Suhr had held 1st base throughout the play and batter May had passed Suhr as May rounded 1st base. The umpires originally called Suhr out at 2nd (on the force) and May out for passing Suhr for an inning-ending double-play! It is unclear if Arnovich's run would have counted under that scenario. The Phillies argued, correctly, that once May passed Suhr and was out, the force no longer applied. After discussing it, the umpires agreed with the Phillies, so Suhr remained on first, Arnovich scored, and May was credited with a sacrifice fly scored as an unassited putout by the Braves 1B Buddy Hassett.

<P><A href="https://www.retrosheet.org/boxesetc/1940/B07192SLN1940.htm">7/19/1940</A> -
In the top of the 4th inning of the second game, Emmett Mueller of the Phillies was the runner on first base with one out in St. Louis. Johnny Rizzo hit a groundball to Cardinals third baseman Jimmy Brown who tossed to second baseman Joe Orengo in an attempt to start an around-the-horn double-play. Orengo stepped on second base but threw wildly to first base allowing Rizzo to reach second base on the error. Thinking he was clearly out on the force at second, Mueller started walking off the diamond toward the Phillies first base dugout. However, the umpire ruled that Orengo had come off second base before he had the ball so Mueller was safe at second. The Cardinals then tagged out Mueller as he was walking towards the dugout which resulted in an inning-ending double-play since Rizzo was declared out for passing Mueller between first and second base (when Mueller was walking off the field).

<P><A href="https://www.retrosheet.org/boxesetc/1942/B08160PHA1942.htm">8/16/1942</A> -
The Yankees loaded the bases with no outs in the top of the 4th inning at Philadelphia. Charlie Keller hit a long flyball over the center fielder's head, scoring two runs. Joe DiMaggio, the runner on first base, held up between first and second base thinking the ball may be caught. Keller made his way to second base but was declared out for passing DiMaggio between first and second base, though he was credited with a two-run single.

<P><A href="https://www.retrosheet.org/boxesetc/1943/B07060CLE1943.htm">7/6/1943</A> -
The Indians had runners on second and third base with one out in the bottom of the 1st inning versus Philadelphia. Ken Keltner hit a comebacker to the pitcher who trapped Oris Hockett between third and home. Hockett stayed in a run-down long enough for Lou Boudreau to reach third base. When the Philadelphia catcher finally ran Hockett back to third, seeing Boudreau already perched there, Hockett kept running past third base and into shallow left field where the catcher dutifully tagged him out. After Connie Mack had a word with the umpires, Boudreau was then declared out for technically passing Hockett when Hockett took his stroll into left field, resulting in a bizarre double-play.

<P><A href="https://www.retrosheet.org/boxesetc/1944/B08201PHA1944.htm">8/20/1944</A> - 
The Athletics loaded the bases with one out in the bottom of the 6th inning versus the White Sox. Ed Busch hit an infield pop-up that White Sox second baseman Roy Schalk dropped. Frankie Hayes, the runner on third base, scampered home. Bill McGhee, perhaps aware of the infield fly rule, remained rooted to second base. George Kell, however, the runner on first base, ran to second base. Umpires ruled a double-play: Busch was out per the infield fly rule and Kell was declared out for passing McGhee. Hayes's run counted.

<P><A href="https://www.retrosheet.org/boxesetc/1945/B04200DET1945.htm">4/20/1945</A> - In the top of the seventh inning, the Indians had runners on 1B and 2B with one out when Eddie Carnett hit a fly to CF. Myril Hoag thought the ball would fall in but it was caught. Hoag passed Mike Rocco beyond 2B for a double play.</P>

<P><A href="https://www.retrosheet.org/boxesetc/1948/B06090SLN1948.htm">6/9/1948</A> - In the bottom of the eighth inning, the Cardinals were 
