<H2>Passing Runners on the Bases</H2>
<P>According to Official Baseball Rule 7.08 (h), any runner is out if he passes 
a preceding runner before that runner is out. This is not an appeal play but is 
called immediately by the umpire. There have been many cases of a runner passing 
a preceding runner through the years. </P>
<p>Many thanks to Rob Wood for finding and writing up most of the recent ones.

