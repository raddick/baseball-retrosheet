<p><b>Retrosheet's Website: Where Do We Go From Here?</b></p>

<p>Retrosheet is in a period of transition. And we need to think about exactly what we want to transition to. This is a golden opportunity to improve what Retrosheet does and what Retrosheet makes available on its website. But, at the same time, we need to be careful not to change things simply for the sake of changing things. If it ain't broke, don’t fix it. And Retrosheet ain’t broke.</p>

<p>With Tom Ruane stepping away from Retrosheet, it would be difficult to maintain the same web pages as in the past. In addition, Retrosheet needs to figure out how to consolidate its AL/NL data with its growing collection of Negro League data.</p>

<p><b>Who Retrosheet Is</b></p>

<p>In thinking about what Retrosheet should provide on its website, it is important to keep in mind who Retrosheet is and what our role is within the greater baseball statistics community.</p>

<ul>
<li>Retrosheet's primary function is as a data provider.</li>
<br>
<li>Retrosheet's focus is the individual game.</li>
<br>
<li>Retrosheet should strive to make data as accessible as possible to as wide a range of potential users as possible.</li>
<br>
</ul>

<p>In keeping with these principles, Retrosheet plans to move toward a model of making any Retrosheet data available on its website available for download. So, for example, Retrosheet hopes to create downloadable versions of the daily batting, pitching, and fielding logs currently available for players. (e.g., <a href=https://www.retrosheet.org/boxesetc/1927/Iruthb1010141927.htm>Babe Ruth's 1927 Batting Log</a>)</p>

<ul>
<li>What information would you like to see available in downloadable player logs?</li>
<br>
<li>What other Retrosheet data would you like to see us make more easily downloadable?</li>
<br>
</ul>

<p>With Tom Ruane stepping back from his role as the primary generator of Retrosheet's website, the website will likely take on a somewhat different look. Our goal is to make this a proactive process to make our already great website even greater.</p>

<ul>
<li>Are you willing to help design and/or maintain Retrosheet's website? If so, please contact me.</li>
<br>
<li>What on the current website do you want to make absolutely certain remains on any redesigned website?</li>
<br>
<li>What parts of the current website could be improved, in your opinion? What, specifically, is needed to make these parts of the website better?</li>
<br>
<li>Is there anything that is not currently available on Retrosheet's website that you would like to see there?</li>
<br>
</ul>

<p>Thank you all very much for your support of Retrosheet. I hope that with your help we can continue to move Retrosheet forward as we continue to move back in history.

<p>Tom Thress<br>
Retrosheet President</p>

