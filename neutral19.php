<P align=center><B><FONT size=5>Alternate Site Games Before 1901</FONT></B></P>
<P>This list shows games played <font color="#FF0000">before 1901</font> by 
major league clubs in a ballpark and/or city that is not home to either team. 
There have been many reasons that these games have been moved through the years 
such as Sunday Blue Laws, park capacity and fires.</P>
<P>This data was compiled by David Vincent from many different sources. If you 
find an error or have data to add, please contact the webmaster.</P>
<P>To see the 20th century games, <a href="neutral20.htm">click here</a>.<br>
To see the ones from 2001 on, <a href="neutral.htm">click here</a>.</P>

