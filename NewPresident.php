<h2>An announcement from Retrosheet</h2>

Retrosheet’s founder, Dave Smith, has decided to step back from his involvement with Retrosheet. As part of this decision, he has resigned as President of Retrosheet. The new President of Retrosheet is Tom Thress, who was Retrosheet’s Treasurer and has been leading Retrosheet’s Negro League efforts. Dave has graciously agreed to remain on Retrosheet’s Board and take on the role of Treasurer.<br><br>

Retrosheet’s longtime Vice President, Tom Ruane, has also stepped down as Vice President but remains on Retrosheet’s Board of Directors. Retrosheet’s new Vice President is Luke Kraemer, who has served on Retrosheet’s Board of Directors since its inception.<br><br>

Retrosheet would not exist without the vision and extraordinary leadership of Dave Smith over the past three decades. Nor would Retrosheet’s work be nearly as complete without the tireless efforts of Tom Ruane. The baseball community owes both Dave and Tom a tremendous amount of gratitude. The world of baseball statistics would simply not be the same were it not for Dave Smith and Tom Ruane.<br><br>

On a personal note, I would also like to thank Dave and Tom for the work they have done, for their friendship, and for serving as wonderful mentors to me in this work. I hope that, under my leadership, Retrosheet can continue to live up to the high standards set by Dave, Tom, and the others who have set such an impressive standard for baseball research.<br><br>

Tom Thress<br>
Retrosheet President


