<b>Using Retrosheet Data</b>

<br><br>

Retrosheet has developed a set of software tools to help parse our event files. These tools can be downloaded <a href=#>here</a>. Documentation for these tools is provided <a href=#>here</a>.
<br><br>

<i>The first link above will be a single zip file containing bevent.exe and bgame.exe. The second link will go to a page that consolidates what is now spread across three different pages discussing how to use mostly bevent. Frankly, I've never found our documentation of our own parsing software to be very user-friendly, but maybe that's just me.</i>

<br><br>

In addition, several baseball researchers have set up repositories of Retrosheet data which is already parsed and which folks may find more helpful to them in working with Retrosheet data. These resources include the following. If you are aware of additional resources which may be of interest to Retrosheet users, please let us know at the e-mail address shown below.

<ul>
<li><a href=https://github.com/chadwickbureau>Chadwick Baseball Bureau</a>
<br>Ted Turocy has created a set of parsing tools which are more comprehensive than bevent. He also maintains a repository of Retrosheet data.<i>I need to ask Ted to give me a better description here.</i></li>
<br>
<li><a href=https://github.com/droher/boxball>Boxball</a>
<br>Boxball is a project designed and maintained by David Roher that makes it easier for users to set up their own Retrosheet databases on their PCs. A single command will automatically create and populate the SQL database of your choice (PostgreSQL, MySQL, etc) with all of the tables produced by the Chadwick software as well as all of the data in the Baseball Databank. It also maintains all outputs in CSV and Parquet format for users who prefer to work directly with flat files.</li>
<br>
<li><a href=https://www.kaggle.com/datasets/jraddick/baseball-events-from-retrosheetorg></a>
<br>Jordan Raddick, the Director of Education and Outreach at the Institute for Data-Intensive Engineering and Science (IDIES) at Johns Hopkins University in Baltimore, has created a series of Kaggles which produce csv files of all plays organized by season.
</li>

</ul>

