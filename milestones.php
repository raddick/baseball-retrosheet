<h2>Milestones</h2>

The lists of milestones show the date reached if we know it exactly
or the year otherwise. Many of the ones for which only the year is shown
require some research, which we plan to do. You can help us out if you
have already done the research and know the date. Please e-mail it to
us at "tthress-ATsign-retrosheet.org".

<p>
If the date is one for which we have the boxscore
posted on the site, there is a link from the date to the box, which in
some cases will also include the play-by-play of the game. If you click
on the player's name, you will be taken the page with his career data.
</p><p>
The Career and Season links for each category display the same page in
different places.

