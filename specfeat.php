leagues and divisions, and their most common nicknames.

<LI>Walter LeConte has developed a list of exhibition games played by major league teams during
the regular season. He has generously provided us with the fruits of his efforts. His articles and
lists were first on our research page. Since then several others have revised the 1871-1920 list.
