<h2>Special Features</h2>
<p>
Here are links to some items that do not fit nicely elsewhere on the site.
There also may be links to them from other pages where you might not think to
look for them.
<ul>
<li><a href="https://www.retrosheet.org/Regional Series">Regional/City Series</a>. An extensive section
about postseason series sanctioned by organized baseball played from 1905-42, mostly in Chicago. There
are boxscores for all 190 of the games in the 32 series, and the vast majority have play-by-play accounts, some deduced. Over 20 years of research by Mike Cantor is the basis of this section about an overlooked part of baseball's history. Also, there are descriptions of other series dating back to 1882 in article by the late Fred Ivor-Campbell.

<li>Charlie Pavitt has compiled an extensive bibliography (over 3000 entries) of "Sabrmetric" research papers (updated in September 2022):
<ul>
<li><A HREF="https://www.retrosheet.org/biblioexplain.pdf">Explanation of the entries and the coding</A>
<li><A HREF="https://www.retrosheet.org/BBREF.xls">Bibliography spreadsheet (Excel file)</A>
</ul>

<LI><A HREF="https://www.retrosheet.org/jackie.htm">Honoring Jackie Robinson</A> in the year of the 60th
anniversary of his major league debut. We show an incredible amount of data about his playing performance.

