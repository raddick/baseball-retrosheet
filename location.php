While most Retrosheet game event files do not have them, hit location
codes are included in some.  The system, which is the same that was used
by Project Scoresheet and the Baseball Workshop, appends the hit location
to the description of the play.  For example "S8/L8S" means a single played
by the center fielder that was a line drive (L) landing in short center
(8S).  The play "7/F78D" is a fly ball caught by the left fielder in deep
left center.  The diagram below shows the hit location codes and contains
additional examples.

