<p class=MsoNormal><b style='mso-bidi-font-weight:normal'>Some notes about Babe
Ruth in ISEGs----<o:p></o:p></b></p>

<p class=MsoNormal style='text-align:justify'>In my 2008 narrative, I stated:
several more homers may be found through further research--hoping to find
#70! Using different research methods, I have now discovered 76 homers for the
Babe in ISEGs!<b style='mso-bidi-font-weight:normal'> </b>Of that total, 69
were hit with the New York Yankees (A), five with the Boston Red Sox (A) and
one each with the New York Giants (N) in 1923 and Brooklyn Dodgers (N) in 1938.
Ruths homers with the Giants and Dodgers are discussed in more detail later in
this section. Among the Babes home runs, three were grand slams. The Babe had
three 3-homer games, 16 ISEGs with two homers and 35 games with one 4-bagger
for a total of 76!<span style='mso-spacerun:yes'> </span>The Bambino hit ISEG
homers in 12 different states and one province (Ontario, Canada). He whacked
the most <span class=SpellE>roundtrippers</span> in New York (15), followed by
Ohio (10), Maryland (7), Connecticut (6) and Indiana (5).<span
style='mso-spacerun:yes'> </span>Fittingly, he hit the most ISEG homers in any
city in his hometown of Baltimore, where he whacked nine. Next in line <span
class=GramE>is</span> Indianapolis (7), Albany (6) and Cincinnati (5). Babe hit
one homer in the Bronx at Yankee Stadium. Leading off the 8<sup>th</sup> inning
against the Giants lefty, Bill Walker, Ruth brought the 60,549 fans to their
feet with a homer into <i style='mso-bidi-font-style:normal'>his</i> place, the
right field bleachers. This game occurred on Wednesday, September 9, 1931
versus the cross-town New York Giants (N), the Bombers winning by a score of
7-3.<span style='mso-spacerun:yes'> </span>The Babe had nine ISEG homers in
three different seasons (1923, 1928 and 1929), eight in 1930, seven in 1931, <span
class=GramE>six</span> in 1924 and five home runs in 1922 and 1927. I doubt if
any other player can remotely come close to the Babes ISEG homer total! Babe
hit 11 in 1923, the most in any one season. The details of all of Ruths <span
class=SpellE>roundtrippers</span> are not presented here at this time.</p>

<p class=MsoNormal>2010, <span class=GramE>ed</span>.</p>

<p class=MsoNormal><b style='mso-bidi-font-weight:normal'><o:p>&nbsp;</o:p></b></p>

<p class=MsoNormal>It should be noted that the Babe hit a homer in 3 games in
which he also pitched.</p>

<p class=MsoNormal style='text-align:justify'><o:p>&nbsp;</o:p></p>

<p class=MsoNormal style='text-align:justify'>As a result of numerous fan
requests, Ruth once pitched in an ISEG.<span style='mso-spacerun:yes'>
</span>On 5/12 1933 at Indianapolis of the American Association, Ruth pitches
the 1<sup>st</sup> two innings, allowing 3 runs.</p>

<p class=MsoNormal style='text-align:justify'><o:p>&nbsp;</o:p></p>

<p class=MsoNormal style='text-align:justify'>At least three games ended prior
to the full 9 innings because of fans swarming the field because of Ruth! In
the game at Binghamton, New York on 5/16, 1929, the Babe hit a 3-run homer in
the top of the 5<sup>th</sup>. After Binghamton was retired in the bottom of
the 5<sup>th</sup>, the fans swarmed onto the field as Ruth rushed from his
fielding position and rapidly made it to the dugout to avoid the fans. The New
York Yankees (A) beat the Triple Cities, 4-1, the game being called after the 5<sup>th</sup>
inning.</p>

<p class=MsoNormal style='text-align:justify'><o:p>&nbsp;</o:p></p>

<p class=MsoNormal style='text-align:justify'>At Toronto on 7/11, 1930, the
Babe had 7 RBI in the 16-11 seven-inning New York Yankee (A) win versus the
Maple Leafs of the International League. In the 2<sup>nd</sup> inning, the
Bronx Bombers scored 11 times, Ruth supplying six of those tallies on a grand
slam and a two-run double. Later, the Babe hit a solo homer to end his 7-RBI
day.</p>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

<p class=MsoNormal style='text-align:justify'>On 7/25, 1938, Ruth hit his 1<sup>st</sup>
homer since leaving the Majors in 1935. The Babe played in a Brooklyn Dodger
(N) uniform, the Albany club of the Eastern League defeating Dem Bums, 7-6. A
record crowd of 11,724 at Albanys Hawkins Stadium saw the Babe blast a 2-run
homer with two out in the top of the 1<sup>st</sup> inning. </p>

<p class=MsoNormal style='text-align:justify'><o:p>&nbsp;</o:p></p>

<p class=MsoNormal style='text-align:justify'>Prior to the game at Baltimore on
5/1, 1930, the Babe and everyone in attendance <span class=GramE>was</span>
entertained by the uniformed band of St. Marys Industrial School, Ruths Alma
Mater. The Orioles scored 4 runs in the bottom of the 9<sup>th</sup> to spoil
the Babes homecoming, the New York Yankees (A) losing to the Birds, 8-7.</p>

<p class=MsoNormal style='text-align:justify'><o:p>&nbsp;</o:p></p>

<p class=MsoNormal style='text-align:justify'>Yankee shortstop, Mark Koenig,
struck blows to the Babe in the 7<sup>th</sup> inning of a game at Baltimore on
9/5, 1926. Ruth did not retaliate. Apparently, Koenig felt Ruth was ragging
him and went after Ruth as he returned to the dugout in the 7<sup>th</sup>.
After the incident, Koenig left the game. The Orioles doubled the New York
Yankees (A), 18-9.</p>

<p class=MsoNormal style='text-align:justify'><o:p>&nbsp;</o:p></p>

<p class=MsoNormal style='text-align:justify'>In Cincinnati (N) on 7/24, 1922,
fans waited patiently, about 2 hours, for the New York Yankees (A) and Babe
Ruth to arrive at Redland Field. Ruth and his gang had their train delayed, but
the fans were eventually rewarded when the Sultan of Swat hit a solo homer in
the 3<sup>rd</sup>!</p>

<p class=MsoNormal style='text-align:justify'><o:p>&nbsp;</o:p></p>

<p class=MsoNormal style='text-align:justify'>Playing in a New York Giants (N)
uniform, Ruth hit a homer at the Polo Grounds versus the Baltimore Orioles on
10/3, 1923.</p>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

<p class=MsoNormal style='text-align:justify'>Ruth was the manager of his New
York Yankees (A) for one day. Playing at the Richmond Colts of the Virginia
League on 5/28, 1923, the Yanks win, 8-5, behind Ruths three hits. He had no
homers.</p>

