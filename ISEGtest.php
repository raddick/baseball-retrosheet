Yank-Lands. The latter team was managed by none other than Babe Ruth, who
appeared in the 6<sup>th</sup> inning and walked. He was then replaced by a
pinch-runner. In the game, witnessed by a crowd of 27,281, the <span
class=SpellE>Cloudbusters</span> blasted the Yank-Lands by a score of 11-5.</p>

