<p class=MsoNormal><b style='mso-bidi-font-weight:normal'>Combined teams<o:p></o:p></b></p>

<p class=MsoNormal style='text-align:justify'>Several ISEGs were comprised of
players from different Major League teams that faced non-Major Leaguers. A good
