<p class=MsoNormal style='text-align:justify'>On Monday, June 16, 2008 a
long-standing tradition in Cooperstown, New York sadly came to an end.<span
style='mso-spacerun:yes'> </span>The Chicago Cubs were to play the San Diego
Padres in the last scheduled Hall of Fame Game ever, but was cancelled due to
rain!<span style='mso-spacerun:yes'> </span>It seems that the powers that be
in Major League baseball decided to end this annual game because of the
complexities of the Major League schedule and its inherent challenges.<span
style='mso-spacerun:yes'> </span>SAY WHAT?!<span style='mso-spacerun:yes'>
</span>Well, so much for a tradition that was in place since 1940!<span
style='mso-spacerun:yes'> </span>An old-timers game was created in its stead.
Good luck with that!<span style='mso-spacerun:yes'> </span>Baseball executives
have said that this last Hall of Fame Game was to be the last exhibition game
due to the Collective Bargaining Agreement; however, I maintain that the annual
All-Star Game is an in-season exhibition game and thusly include them on my
ISEG list found elsewhere on this site.</p>

