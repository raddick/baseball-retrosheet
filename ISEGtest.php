<p class=MsoNormal><b style='mso-bidi-font-weight:normal'>1st or 2<sup>nd</sup>
games of doubleheaders<o:p></o:p></b></p>

<p class=MsoNormal style='text-align:justify'>In-Season Exhibition Games were
sometimes played as part of a doubleheader to attract a greater crowd as well
as to sponsor some sort of charitable endeavor. Generally, the Major Leaguers
would play the non-Major League club that was usually hosting the contest.
However, there was a <span class=SpellE>twinbill</span> that was an exception
to this rule, because it was the only game discovered that involved <b
style='mso-bidi-font-weight:normal'>four different </b>Major League teams
playing in a Minor League host city! In fact, the newspapers confirmed the
originality of these two games as the first interleague doubleheader
exhibitions for charity. The proceeds of the games went to the erection of an
addition to the local Hillcrest Hospital. On Monday 6/14, 1937, these games
were played in Charleston, West Virginia, which was the home of the Charleston
Senators of the Middle Atlantic League. (An aside is the fact that the Senators
were a farm team of the Detroit Tigers in spite of possessing the nickname
associated with the Washington American League club). Now on to the game! The
four Major League clubs involved were the Philadelphia Phillies (N)the winner,
9-5--versus the Washington Senators (A) in the 1<sup>st</sup> game. This was
followed by the Philadelphia Athletics (A) pitted against the Cincinnati Reds
(N)the winner, 6-4-- in the nightcap. An estimated crowd of 6,000 were
entertained for several hours by this historic event. Boy, what a memorable
afternoon in Charleston! This may be the only ISEG of this type4 different
teams, 2 AL, 2 NL---ever played and perhaps a second such game may be
uncovered! However, only time will tell, especially as research using digitized
newspapers is always speedily evolving! So far, Ive uncovered 42 doubleheaders
and they are listed on the ISEG spreadsheet in the column labeled with the #
mark.</p>

