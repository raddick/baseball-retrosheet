<H2>Resumed Protested Game: 6/5/1943</H2>
<p>
With the score 1-0, SL ahead in the middle of the 8th, the game was called because of rain. The Phils claimed
the Cards did not properly cover the field, and the protest was upheld. The game resumed on July 29 at the start of the home 8th.
The Cards did not score. Wasdell homered to start the 9th and the Phils scored again on an RBI Single by May.
In the last of the 9th Wasdell completed his heroic mini-game by throwing out Litwhiler at the plate as he tried to
score from 2nd with 2 out. The 1-0 SL win became a 2-1 loss.


