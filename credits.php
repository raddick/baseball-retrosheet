Retrosheet has a lot of friends and nearly all of them have
contributed to this section. Since one of the two basic sources
used in this section is the game logs, it is appropriate to start by
thanking Bob Tiemann, who originated the logs, and Arnie Braunstein,
who computerized them. It should be noted that those who
contributed original research to this effort were not also responsible
for the format of its presentation or for any errors that were
introduced in the preparation of material for publication.
 
Much has been added to the original logs and here is a summary
of some of the people and groups contributing significant research:
 
*) SABR Biographical Committee provided the information on players'
vital statistics (birth, death, and so on).
*) Stats Inc. donated their log of starting and winning pitchers since
1920.  They also donated their play-by-play data for 1991 and 1992.
*) Major League Baseball Advanced Media (MLBAM) graciously donated the
event file play by play data for the 1993-1998 seasons.
*) Rich Pray sent us his data on starting pitchers from 1883-1919.
In addition to mailing us his box of sheets (at his own cost), he
has been patient and helpful throughout the process.
*) Frank Vaccaro sent us his game logs, with starting and winning
pitchers (and much more), covering the 1871-1908 period.  Besides
being fascinating work in its own right, his logs helped us enormously
during the proofing of the 19th scores as well as the 1901-08 starting
pitcher data.
*) In addition to originally starting these logs, Bob Tiemann
also entered all the umpire data from 1986 and 1987, and
helped us correct the home park data for the years during the
1930s and 1940s when the Cleveland Indians had two home parks.
*) Bob Tiemann and Pete Palmer are also responsible for the 1872
and 1874 box scores on the site and of course Pete is the source of
all the Batter-Fielder Wins and Pitcher Wins data shown on the site.
*) Mark Armour went through all our games from 1980-1983 and filled in
the holes (and there were LOTS of them) in our umpire, game length,
day/night and attendance data.
*) Ted Turocy (1984 and 1987), Marc Stephenson (1986) and John
Stryker (1985) filled in all the 1984 and 1987 attendance and game
length data from The Sporting News Box Score books.
*) David Ball permitted us to use his extensive research on 19th
century player transactions.
*) Lyle Spatz and Cliff Blau have done an enormous amount of research
into transactions and were kind enough to share their work with us.
*) The late Bob Boynton, who passed away in the fall of 2006, supplied
us with research data on umpires, attendance and length of games for many
seasons prior to 1960.
*) Clem Comly did an extraordinary amount of work in proofing the details
of more than 15 seasons, tracking down every discrepancy between our data
and the official totals.
*) The baseball gaming community, most especially the Strat-O-Matic
(SOM) and Diamond Mind researchers, have generously contributed their
transaction research. An incomplete list of these contributors
include: Len Durrant, Joe Freitas, Glen Guzzo, Charlie Hall,
Sean MacNair, Joe Moran, Rob Ratliff, Terry Simkins, Gary Simonds,
Stephan Weightman, Ken Wenger and Jim Wheeler.
 
