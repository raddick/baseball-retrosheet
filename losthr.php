<H3><A name=Weather></A>Weather</H3>
<p>5/16/1885: Tip O'Neill of St. Louis Browns (AA) homered in the fourth inning against the Baltimore Orioles. The game, played in St. Louis, was rained out.

<p>7/14/1908: Josh Clarke of the Indians hit a homer in the first inning in New York but before the top half of the frame was completed, the game was washed out by heavy rain.

<P>5/29/1913: Art Devlin hit a home run in a game against Brooklyn in Boston 
with a man on base. This blast came in the second inning but was lost to 
rain.

<p>7/21/1916: Brooklyn was rained out in Pittsburgh in the fourth inning. Billy Hinchman of the Pirates smashed a solo homer in the bottom of the third off Jack Coombs. In the bottom of the first frame, Max Carey was ejected by Al Orth over a call at first base.

<p>7/17/1922: Joe Judge hit a grand slam in the bottom of the first in a game against the Browns. Heavy rains
canceled the game in the bottom of the third with Washington ahead 5-1.

<P><A href="https://www.retrosheet.org/boxesetc/1922/B08040BSN1922.htm">8/4/1922</A>: 
Ray Powell of the Braves hit an inside-the-park home run in the bottom of the ninth inning
but the inning was wiped out when they could not finish. The rain made play impossible and the game reverted
back to the end of the eighth inning with the Pirates winning, 3-0.

<p><A href="https://www.retrosheet.org/boxesetc/1924/B07310NY11924.htm">7/31/1924</A>: 
Kiki Cuyler lost a home run when a game was rained out and
the score reverted back to the previous inning. The Pirates were playing
in New York when the storm appeared in the third inning. The Giants,
behind 5-0 at this point, started stalling to try to prevent the game
from becoming official. In the top of the seventh, Cuyler homered into
the lower right field seats off Jack Bentley. However, the game was
called after a 30-minute rain delay in the bottom of the frame and the
seventh inning was wiped off the books.

<P><A href="https://www.retrosheet.org/boxesetc/1925/B05160DET1925.htm">5/16/1925</A>:
With the Yankees playing in Detroit, Earle Combs hit a solo homer into the right field
bleachers off Ed Wells to lead off the top of the ninth inning. Wells had just entered
the game. However, with two out in the bottom of the frame, home plate umpire Brick
Owens stopped the game due to the weather conditions, since there lightning and thunder
in the area and the sky had grown dark due to the storm. Combs lost what would have been
his first major league dinger to the poor conditions. Eleven other batters' statistics
were affected by the loss of the ninth inning.

<P>6/29/1925: 
Giant Travis Jackson homered to lead off the bottom of the second 
inning at the Polo Grounds off Bill Ryan of Boston. The game was halted later in 
the half-inning and then cancelled by Umpire Bill Klem because of the field 
conditions after a heavy rain.

<P>7/9/1925: St. Louis Brown player-manager George Sisler homered into the right 
field bleachers at home off the Yankees' Herb Pennock. The blow came in the 
first inning with one man on base. The game was rained out in the fourth inning. 

<P><A href="https://www.retrosheet.org/boxesetc/1925/B07150PHI1925.htm">7/15/1925</A>:
Tommy Griffith of the Cubs homered off Johnny Couch in 
Philadelphia with one out and no one on in the seventh inning. The bottom of 
that inning was not completed and the game called because of rain so the score 
reverted back to the previous inning and Griffith's home run was eliminated. 

<P>7/22/1925: In a game at Philadelphia both the Phillies' Lew Fonseca and the 
Pirates' Pie Traynor lost homers to the rain. Fonseca hit a solo shot off Vic 
Aldridge in the second inning. Traynor's blast came in the third with a man 
aboard. The game was called in the bottom of the fifth with the Bucs ahead 7-3. 

<p>10/4/1925: Al Simmons of the Athletics smacked a solo homer off Dutch Ruether
in Washington in the top of the fourth inning. The game was washed out after
the Senators batted in the bottom of that frame.

<p>6/23/1926: The Phillies and the Giants attempted to play a doubleheader at the
Polo Grounds but rain washed it out. The Phils had scored five runs in the first
inning, including a three-run homer by George Harper off Virgil Barnes.

<P>6/21/1928: Babe Ruth hit his 26th home run of the season in the first inning 
of game two at Yankee Stadium. This two-run blast tied the game with the 
Philadelphia Athletics, 2-2. Philadelphia scored again in the top of the third 
inning but the game was called because of rain and the muddy conditions.

<P><A href="https://www.retrosheet.org/boxesetc/1928/B06220CHN1928.htm">6/22/1928</A>: 
In the top of the seventh inning at Wrigley Field, the Cardinals 
scored two runs on a homer by Jim Bottomley to take a 6-1 lead over the Cubs on 
Ladies Day. However, rain stopped the game during the top half of that frame and 
the score reverted back to the last complete inning, so Bottomley lost his 
sixteenth home run of the season.

<p><A href="https://www.retrosheet.org/boxesetc/1932/B06010CHN1932.htm">6/1/1932</A>:
The Cardinals beat the Cubs, 1-0, at Wrigley Field in a seven-inning,
rain-shortened game. Pepper Martin and Eddie Delker both homered in the top
of the eighth inning but lost them when the game was called and the score
reverted to the seventh inning. Delker was 0 for his last 18 at the time
of the homer and had never slugged a four-bagger in the majors at the time.
 
<P>6/28/1933: Roy Johnson of the Red Sox hit a solo shot off Bump Hadley of the 
Browns in the top of the fourth inning. The ball landed on the roof of 
Sportsmans Park. That shot gave the Sox a 4-3 lead but the game was washed out 
after four innings.

<p><A href="https://www.retrosheet.org/boxesetc/1934/B05290PHA1934.htm">5/29/1934</A>:
In the top of the eighth, Dick Porter and Rick Ferrell of Red Sox hit homers.
However, before the inning was over, heavy rains came, and after a 44 minute wait
the game called. The score after seven innings was 2-1 in favor of the Red Sox,
so that was how the game ended.

<p>6/12/1934: Babe Ruth homered at Yankee Stadium off Bobo Newsom of the
St. Louis Browns. The drive came in the fourth inning to start the scoring
for the home team. After the frame, the Yankees led, 3-1, but rain washed
out the remainder of the contest. This would have been Ruth's tenth home
run of the season. Debs Garms of the Browns also had hit a homer before the
game was terminated. 

<p><A href="https://www.retrosheet.org/boxesetc/1935/B05290WS11935.htm">5/29/1935</A>:
The Yankees scored three runs in the top of the seventh against the Senators. In the
bottom of the inning Jake Powell hit a homer. The rain, which had started to fall
earlier, became so hard that the game could not be continued. The score reverted
back to the end of the sixth inning and 7-4 New York win.
 
<P>6/16/1935: Lou Gehrig and Frank Crosetti each lost a homer because of rain. 
As part of a seven-run fourth inning in the first game of a scheduled 
doubleheader, Crosetti hit his shot off Ted Lyons with one on. Gehrig's drive 
came as the first batter to face Leslie Tietje, also with one man on base.

<p>6/6/1937: Al Simmons of the Senators hit a grand slam in the top of the
first inning in the second game at Cleveland. The contest was washed out
after three innings.
 
<P>8/21/1937: Jimmie Foxx led off the bottom of the second inning with a homer 
off Jimmy DeShong that cleared the left field wall and the street behind Fenway 
Park. It was the only run that the Red Sox scored in four innings as the 
Senators were winning, 5-1, when umpire George Moriarty called the game due to a 
torrential downpour.

<p>5/27/1939: Hank Greenberg of the Tigers hit a two-run home run off Browns
pitcher George Gill in the bottom of the third inning. The game was rained
out after 3-1/2 innings with Detroit leading 4-3.

<p>5/21/1940: Frankie Hayes of the Athletics hit a grand slam off Browns pitcher
Bob Harris in the top of the fifth inning. The game was rained out after 4-1/2
innings with Philadelphia leading 6-0.

<p>6/15/1945: The second game of the Dodgers and Braves double header in Boston
was rained out after three innings. In the second, Chuck Workman had homered off
of Tom Seats. The game was tied 3-3 when it was called.

<p>8/26/1947: In the top of the second of the second game of a doubleheader,
Clyde McCullough of the Cubs hit a home run off of Giants hurler Sheldon Jones.
The game was rained out in the bottom of the inning with the Cubs ahead 1-0. 

<P>8/29/1947: Cleveland's Hank Edwards hit a two run homer with no one out in 
the bottom of the first inning of a game at Municipal Stadium. The game was 
stopped four batters later because of rain. 

<P>9/21/1947: Three home runs were lost due to a rainout in Detroit. The 
Indians' Joe Gordon and Les Fleming hit back-to-back shots to lead off the 
second inning off Hal Newhouser. Vic Wertz of the Tigers had hit a
run blast in the first off Don Black. Those were the only runs when the game was 
stopped in the top of the fourth. 

<P>5/12/1948: Joe DiMaggio hit a three-run homer in the bottom of the first 
inning at Yankee Stadium off Gene Bearden of the Indians. There was one out at the time. The 
game was rained out in the bottom of the fourth.

<p>6/26/1949: Dino Restelli of the Pirates lost a home run during Game Two of the
June 26, 1949 Brooklyn at Pittsburgh doubleheader. The game was cancelled due to
rain and the curfew after three innings. Restelli had hit a home run in the
first inning off of Rex Barney. The game was delayed by rain in the second
inning and then stopped with the Dodgers leading 5-4 after three innings.
The Pennsylvania Sunday curfew law prohibited any inning from beginning
after 6:45 pm (or any play after 7:00 pm).

<P>7/27/1949: Three homers were lost in a rain-shortened game at Yankee Stadium. 
In the bottom of the first, Tommy Henrich homered off Cleveland's Bob Feller 
with two on and no one out. In the top of the second, Joe Gordon hit a solo shot 
with one out off Ed Lopat. One inning later, Cleveland pitcher Sam Zoldak also 
hit a solo homer with one out off Lopat. 
<P>7/29/1949: Ralph Kiner hit a two-run home run in the bottom of the first 
inning at Forbes Field before 30,000 fans. In the top of the fourth a heavy 
downpour halted play with two outs and the 2-0 lead still intact for the 
Pirates. That would have been Kiner's 28th homer of the season in the year that 
he hit 54.
 
<P>8/28/1949: Larry Doby of the Indians homered off Boston's Joe Dobson in the 
bottom of the third inning with two out and no one on. The game was called in 
the top of the fourth because of darkness. It was the second game of a double
header after the first game took over three hours with a long rain delay. 

<P>7/24/1950: In a game that had been rescheduled from 5/10 due to rain in 
Pittsburgh, Eddie Waitkus lost a home run. In the top of the seventh inning with 
the Pirates ahead, 2-1, the Phillies rallied to take the lead as Waitkus hit a 
two-run shot with two outs. There had been a 34-minute rain delay at the end of 
the second inning and after the homer, the game was delayed for an hour and two 
minutes before it was called. According to the rules, the score reverted back to 
the end of the sixth inning since the lead had changed and the home team had not 
had its turn at bat in the inning. Thus all events in the top of the seventh 
were eliminated.

<P>8/29/1950: Tommy Byrne, a Yankee pitcher, hit a home run in the bottom of the 
second inning of the second game of a scheduled doubleheader. It came with 1 on 
and 1 out off Sam Zoldak and put the Yanks ahead 4 to 0. In the bottom of the 
third, the game was called because of rain. 

<p><A href="https://www.retrosheet.org/boxesetc/1950/B09100PHI1950.htm">9/10/1950</A>:
The Phillies' Jackie Mayo homered in the bottom of the sixth 
inning before the game was called due to rain. Since the sixth inning had not 
been completed, the score (and stats) reverted back to the previous full inning 
because the lost homer left the Phillies still trailing, by a score of 3-2. 

<P>5/23/1951: The Tigers were ahead 7-3 at Yankee Stadium when the game 
was washed out in the top of the fourth. Both Bud Souchock and Joe DiMaggio lost homers. 

<P>6/1/1951: Vic Wertz led off the bottom of the second in Detroit with a shot 
off Allie Reynolds. Johnny Mize homered to lead off the Yankee fourth against 
Virgil Trucks. The game was rained out after 4 1/2 innings. 

<P>8/25/1951: At the Polo Grounds two batters had homers rained out. Bobby 
Thomson homered off Cardinal Al Brazle with one out in the second inning and 
Stan Musial hit one off Al Corwin to lead off the third inning. 
<P>6/21/1952: Steve Souchock of the Tigers lost a two-run homer to the rain. The 
game against the Athletics was called in the bottom of the fourth. 
<P>8/2/1952: Hank Sauer hit a homer at Brooklyn in the third inning with two on 
base. The pitcher was Ben Wade. The game was rained out in the fifth inning. 
Sauer ended the season tied for the home run lead with Ralph Kiner with 37. 
<P>5/12/1953: Carlos Bernier of the Pirates led off the top of the second inning 
with a homer off Cincinnati's Eddie Erautt for the first run of the game. With 
one out in the bottom of the fifth and the Pirates ahead, 5-1, the game was 
rained out. Bernier hit three homers in his career. 

<P><A href="https://www.retrosheet.org/boxesetc/1953/B08030MLN1953.htm">8/3/1953</A>: 
Duke Snider lost a homer to the rain in Milwaukee. The blow came as 
he led off the eighth inning with Lew Burdette pitching for the Braves. The game 
was called with the Dodgers leading 2-0 during the bottom of the eighth. The records
reverted back to the end of the seventh and a 1-0 Dodgers win.

<p>4/20/1955: Felipe Montemayor of the Pirates hit a three-run home run
(which would have been his first major league homer) off Giants pitcher Ruben Gomez
in the bottom of the third inning. The game was rained out in the top of the
fourth inning with Pittsburgh leading 3-0.

<p>4/24/1955: In the second game of a scheduled double-header, Dee Fondy and
Harry Chiti of the Cubs both hit home runs off Reds pitcher Corky Valentine
in the bottom of the second inning. The game was rained out in the bottom
of the third inning with Chicago leading 3-0.

<P>5/25/1955: Pittsburgh's Gene Freese rounded the bases while the Dodger 
outfield tried to track down his hit at Forbes Field. It came off Billy Loes in 
the second with no one on and was rained out. 
<P>6/12/1955: In what would have been the second game of a doubleheader in 
Philadelphia, Wally Post of Cincinnati homered off Ron Negray of the Phillies in 
the first with one out and two on base. Andy Seminick homered off Rudy Minarcin 
in the bottom of the second with one out and no one on base. The game was washed 
out in the bottom of the third inning.

<P>7/23/1955: The Senators were rained out in Detroit in the second game of a
doubleheader. Eddie Yost led off
the game with a homer and Frank Lary hit one in the bottom of the third.
It would have been Lary's first career home run. The game was called in
the top of the fourth inning.
 
<P>8/13/1956: Peewee Reese homered in the top of the first inning with no one 
on and one out but had it rained out.
 
<P>4/26/1957: Cal Neeman homered in the second inning off the Cardinals' Sam 
Jones in Wrigley Field. There 2 were out and 1 man on base at the time. The game was 
rained out. 

<P>8/18/1957: Bob Speake homered in the bottom of the first at Wrigley Field
in the second game of a doubleheader. This two run homer was lost to rain.
 
<P>6/1/1958: Al Kaline homered to lead off the bottom of the second inning 
against Chicago's Ray Moore but the game was rained out after 3 1/2 innings. This 
homer would have given Kaline 400 for his career.

<P>6/18/1958: Washington's Neil Chrisley hit a home run off Detroit's George 
Susce in the top of the first with one on and one out. The game was rained out 
in the top of the fourth.
 
<P>6/25/1958: Bob Boyd and Gus Triandos of the Orioles homered off Jim Bunning 
at Tiger Stadium in the first inning only to see them washed out. Boyd's blast 
came with one out and no one on while Triandos hit his two batters later with a 
man aboard. Bunning didn't last through the inning. 
<P>6/12/1959: Harmon Killebrew hit a homer to left field with two outs in the 
bottom of the first inning of a game in Washington. It came off Herb Score of 
the Indians. The game was halted by rain after that hit. 
<P>8/23/1959: In game 2, the Orioles' Bob Nieman homered to right field off Jim 
Bunning of Detroit in the top of the first inning. The game was rained out in 
the bottom of the first. 
<P>5/6/1961: The Senators were rained out in Cleveland in the bottom of the 
second inning. In the first frame, the Tribe's Woodie Held smashed a grand slam 
over the right field fence off Hal Woodeshick. The blow came with no one out. 
The Nats scored an unearned run in the second, leaving the score 4-1 at the time 
of the postponement. 

<P>7/17/1961: The Yankees were leading 4-1 in the top of the fifth
in Baltimore when the game was rained out. In that game, scheduled as the
second game of a doubleheader,
both Roger Maris and Mickey Mantle hit homers off Hal Brown. Maris' came in the
first with two out and no one on while 
Mantle blasted his leading off the fourth. In addition Marv Throneberry of the 
Orioles homered in the second with the bases empty as did Clete Boyer of the 
Yankees in the 3rd, making a total of 4 homers lost to rain in this one game. 

<P>8/6/1961: In a game at Baltimore's Memorial Stadium, two hitters lost 
home runs to rain: Jerry Adair of the Orioles and Tom Satriano of the Angels. It 
would have been Satriano's first major league homer. 
<P>5/28/1962: Bobby Del Greco homered off Yankee Bud Daley in Kansas City but 
lost it to rain. The drive came in the first inning with one out and no one on 
base. 
<P>6/24/1962: In the third inning of a game at New York, Houston's Norm Larker 
hit a home run with 2 men on and 2 out off the Mets' Roger Craig. This game was 
rained out. 
<P>4/17/1963: Al Luplow had a homer rained out against the Senators in 
Cleveland. The hit came off Tom Cheney with one out and no one on in the first 
inning. 
<P>4/29/1963: In a game at Comiskey Park, Chicago centerfielder Jim Landis led 
off the first inning with a homer to left. It came off Yankee Bill Stafford and 
was the only hit in the one and a half innings played before the game was rained 
out. 
