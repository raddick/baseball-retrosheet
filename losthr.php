<H2>Lost Home Runs</H2>
<P>Through the years, many batters have hit home runs that do not appear as part 
of their record. Most of these apparent homers were lost due to inclement 
weather conditions such as rain-outs. However, there have been quite a few 
four-baggers that have been ultimately credited as some other event in the 
record of the game due to human error on the field. Base-runners and umpires 
account for most of these errors. 
<P>The following is an incomplete list of home runs that were lost for one 
reason or another. Go to those affected by <A 
href="https://www.retrosheet.org/losthr.htm#Weather">weather</A>; go to those 
lost due to <A href="https://www.retrosheet.org/losthr.htm#Other">other 
causes</A>; go to those from the <A 
href="https://www.retrosheet.org/losthr.htm#Minors">minor leagues</A>. 
