umpires. After having homered in the third, in the bottom of the sixth Alex Rodriguez hit of line shot to
right center off of Lance Cormier that cleared the fence, but bounced back into the field off of a set of
stairs above the fence painted yellow. Orioles right field Nick Markakis played the ball and threw to second
where Rodriguez was standing. Immediately after the play, Rodriguez pointed to the stairs and said the ball
had hit there. After a brief consultation the umpires ruled the ball had bounced back from the top of the
fence, so the hit became a double. However, replays showed the ball cleared the fence by a couple of feet.

<P><A href="https://www.retrosheet.org/boxesetc/2010/B05190TEX2010.htm">5/19/2010</A>:
Josh Hamilton of the Rangers homered in the second inning against the Orioles
in Arlington. In the fourth, his fly cleared the 14-foot fence in LF and caromed back onto
the field. Second base umpire Doug Eddings ruled it in play. Rangers manager Ron Washington
argued with Eddings, who stood firm in his call, saying the ball hit the pad. Washington
was convinced and left the field. Crew chief Dana DeMuth did not watch the replay and
admitted after the game that the unpires were wrong. He said that he should have watched
the replay and then overturned the call.

