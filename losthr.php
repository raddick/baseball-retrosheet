<p>7/3/1949: Quentin Martin, playing for Rocky Mount of the Coastal Plain League,
hit his fourth home run of the game in the ninth inning at Goldsboro. Shortly
after that, it began to rain and, eventually, the game had to be called. Rocky
Mount was ahead at the time, so all ninth inning statistics were wiped out,
including Martin's fourth home run

<P>8/10/1958: Las Vegas third baseman Elmo Plaskett hit a home run in a 
California League game at Fresno off Tom Fitzgerald. The hit came with one on 
and one out in the second inning. After crossing the plate, Plaskett realized he 
had missed the first base bag and circled the bases again to make up for it. 
When a new ball was put in play, Fresno appealed and Plaskett was called out on 
appeal. 
<P>1981: Rick Stuart of the Johnson City Cardinals hit the only homer of his 
professional career over the left field fence. As his teammates gathered around 
home plate to congratulate him, he failed to touch the plate and was called out 
on appeal. Stuart is the son of former major leaguer Dick Stuart.

<P>6/22/2006: Chris Carter of the Great Falls White Sox (Pioneer League) hit a 
ball over the left field fence in Billings with Lee Cruz running at first base. 
Cruz thought the ball might be caught and was returning towards the bag when 
Carter rounded first while celebrating the homer. Carter passed Cruz and was 
called out and credited with an RBI-single. He led the league with 15 home runs 
for the season.

