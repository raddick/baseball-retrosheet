        <h2>Most Wanted Games</h2>

      <IMG SRC="badgball.jpg" align="right" height="300">

     <p>With our recent addition of the last game we needed from 1973, we now have
game accounts (play-by-play descriptions of the games)
for all American and National League seasons from 1973-2021.
We have accounts for well over half of the games in all seasons back to 1914.
Our goal is to get accounts for every major league game ever played. 
To help us find the games we still need,
we are listing the games for which we do <i>not</i> have
a complete game account in the 1920-72 seasons. Please e-mail us
("dwsmith-ATsign-retrosheet.org")
if you have a play-by-play account of one of our most wanted games
or know who might have one.
We would even welcome an incomplete play-by-play account for only
some of the innings of these games.
<p>
If you have an account of a game played before 1920 and would like
to know if it is among our needed
games, please let us know by sending e-mail to
"dwsmith-ATsign-retrosheet.org".<p>

Last update: August 5, 2022
