<h2>Early Batting Order Practices</h2>
<p>
In the early days of major league play, quite a few of the rules and
accepted practices were quite different from those in place at the
beginning of the 20th Century, which is usually considered to be
the start of the modern era. Here are some notes
about the order in which the players batted.
<p>
Before 1879, the batting order rule specified that "After the first inning, the first striker in each
inning shall be the batsman whose name follows that of the third man out in the preceding inning."
If the third man put out was a baserunner and not the last batsman, one or more teammates would
bat again at the beginning of the next inning, in a way that would be "batting out of order" today.
<p>
For example, the record for plate appearances in one game is ten by West Fisler, batting fifth for the
Athletics at Troy. The first four batters appeared only nine times each, Fisler once leading off the next
inning after the fourth batter made the third out on base. On another occasion, the seventh and eighth
batters finished with five and two plate appearances!



