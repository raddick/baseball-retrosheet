<P align=center><B><FONT size=5>Game-Ending Hits</FONT></B></P>
<P>Before 1920 when a team won the game by scoring in the bottom of the last 
inning, the rules stated that they could not win by more than one run. A player 
hitting a home run to end the game might have been given credit for a lesser hit 
depending on the number of bases advanced by the runner who scored the winning 
run. The hit was a homer if the batter's run was the game-winner. However, if a 
runner already on base at the time of the game-ending hit scored the winning 
run, the hit was not a home run according to the rule. Thus no one actually 
“lost” a homer. The following is the list of known instances of these 
game-winning hits.</P>
