<h2>Retrosheet News and Newsletters</h2>
<p>
Until early 2002, we mailed and posted newsletters on a more or less regular
basis. They are available from links that appear later on this page. This page
also includes some lists of some material that has appeared in the newsletter
and is of the same type that would have appeared had we continued to publish
them. Although they are not of recent vintage, the newsletters contain items
that will be of interest to all baseball fans in addition to Retrosheet news.
We urge you to read through them because we think you will be pleasantly
surprised by what you find.
<p>
The newsletters are available in text and/or HTML formats.  The HTML format provides
a table of contents, navigational aids, and a more attractive format.  However, some
internet viewers may not display the HTML well.  If so, return here and and choose the
text version.  You may want to download the text version, import it into your usual
word processor and then print it.
<p>
Starting with Volume 5 No. 3, they are also available as Adobe Acrobat
(.pdf) files.  These are almost exact images of the printed editions.  Click on "PDF" to
download the Acrobat files. To see and print them, you will need the Adobe Acrobat Reader.
It is probably already installed on your system, but if not, it is a free download:
<A HREF="https://www.adobe.com/prodindex/acrobat/readstep.html">Download Acrobat Reader</A>


