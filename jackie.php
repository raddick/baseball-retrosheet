<h2>Honoring Jackie Robinson</h2>
<p>
In honor of the 60th anniversary of his breaking the major league color barrier, we present
(as a permanent page) his batting splits and other details about his regular season performance.
The information on this page is based on the wonderfully detailed scoresheets kept by Allan
Roth, who was Branch Rickey's right hand man and the most noted statistical analyst before
Bill James.
<p>
Additional data, including his record in World Series and All-Star Game play, can be found on
his page in the <A HREF="https://www.retrosheet.org/boxesetc/R/Probij103.htm">Directory of Players</A> 
