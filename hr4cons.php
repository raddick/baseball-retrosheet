<H2>4 Homers in a Row</H2>
<P>Back-to-back-to-back-to-back homers have happened nine times in
major league play.

<p>Click on the date to see the boxscore and play-by-play when available.
<p>

<P><a href="https://www.retrosheet.org/boxesetc/1961/B06080CIN1961.htm">6/8/1961</a>
 - Braves on the road against the Reds in the top of the 7th:
<ol>
<li>Eddie Mathews (off of Jim Maloney)
<li>Hank Aaron (Maloney)
<li>Joe Adcock (Marshall Bridges)
<li>Frank Thomas (Bridges)
</ol>
<p>
<P><a href="https://www.retrosheet.org/boxesetc/1963/B07312CLE1963.htm">7/31/1963</a>
 - second game, Indians at home against the Angels in the bottom of the sixth :
<ol>
<li>Woodie Held (off of Paul Foytack)
<li>Pedro Ramos (Indians pitcher) (Foytack)
<li>Tito Francona (Foytack)
<li>Larry Brown (Foytack)
<br>Note: Foytack was relieved after giving up the fourth one.
</ol>
<p>
<P><a href="https://www.retrosheet.org/boxesetc/1964/B05020KC11964.htm">5/2/1964</a>
 - Twins on the road against the Athletics in the top of the eleventh:
<ol>
<li>Tony Oliva (off of Dan Pfister)
<li>Bob Allison (Pfister)
<li>Jimmie Hall (Pfister)
<li>Harmon Killebrew (Vern Handrahan)
</ol>
<p>
<P><a href="https://www.retrosheet.org/boxesetc/2006/B09180LAN2006.htm">9/18/2006</a>
 - Dodgers at home against the Padres in the bottom of the ninth:
<ol>
<li>Jeff Kent (off of Jon Adkins)
<li>J.D. Drew (Adkins)
<li>Russell Martin (Trevor Hoffman)
<li>Marlon Anderson (Hoffman)
</ol>
<p>
<P><P><a href="https://www.retrosheet.org/boxesetc/2007/B04220BOS2007.htm">4/22/2007</a>
 - Red Sox at home against the Yankees in the bottom of the third:
<ol>
<li>Manny Ramirez (off of Chase Wright)
<li>J.D. Drew (Wright)
<li>Mike Lowell (Wright)
<li>Jason Varitek (Wright)
<br>Note: It was Drew's second time, and Red Sox manager Terry Francona is the son of Tito Francona who
was part of the Indian's quartet in 1963.
</ol>
<p>
<P><P><a href="https://www.retrosheet.org/boxesetc/2008/B08140CHA2008.htm">8/14/2008</a>
 - White Sox at home against the Royals in the bottom of the sixth:
<ol>
<li>Jim Thome (off of Joel Peralta)
<li>Paul Konerko (Peralta)
<li>Alexei Ramirez (Peralta)
<li>Juan Uribe (Robinson Tejeda)
</ol>

<p><p><a href="https://www.retrosheet.org/boxesetc/2010/B08110MIL2010.htm">8/11/2010</a>
 - Diamondbacks on the road against the Brewers in the top of the fourth:
<ol>
<li>Adam LaRoche (off of Dave Bush)
<li>Miguel Montero (Bush)
<li>Mark Reynolds (Bush)
<li>Stephen Drew (Bush)
<br>Note: Stephen Drew is the the brother of J.D. Drew, who has been part of four in a row twice.
They are the only brothers to have participated in such a string of homers.
</ol>

<p><p><a href="https://www.retrosheet.org/boxesetc/2017/B07270WAS2017.htm">7/27/2017</a>
- Nationals at home against the Brewers in the bottom of the third:
<ol>
<li>Brian Goodwin (off of Michael Blazek)
<li>Wilmer Difo (Blazek)
<li>Bryce Harper (Blazek)
<li>Ryan Zimmerman (Blazek)
<br>Note: After an out Anthony Rendon also homered off of Blazek for five blasts in six batters.
</ol>

<p><p><a href="https://www.retrosheet.org/boxesetc/2019/B06090SDN2019.htm">6/9/2019</a> - Nationals on the road against the Padres in the top of the eighth:
