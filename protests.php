<H2>Resumed Protested Games</H2>
<p>
Most protests of games are not upheld. There are many reasons for this, with the most frequent two being:<p>
1. The protest had an incorrect basis, such as a disagreement with an umpire's judgment, which cannot be protested.<p>
2. The team lodging the protest won the game, so it was never formally filed.
<p>
There have been some games when the protest has been
upheld, even when the umpires correctly enforced the rules.
Perhaps the most famous case is the George Brett pine tar bat homer on 7/24/83.
In that case, the umpires were following the existing rules to the letter
when Brett's homer was negated and he was called out.  However, AL President
Lee MacPhail ruled that the penalty was not within the "spirit of the rules"
and reversed the call.  Brett was awarded the home run and the game was
resumed from that point (with some interesting substitutions by Yankee manager Billy Martin).
<p>
When a protest is upheld, the game is supposed to be played from the
point of the protested play as corrected. However, there have also been times when the
protest has been upheld, but the
game has not been resumed. One infamous example was the Cardinals at
Dodgers game on 7/20/47 where both teams batted in the ninth, but
neither made three outs! There is a brief description after the list of resumed
protested games.
<p>
Jim Smith has provided a number of protested games that were picked up from the
point of protest at a later date, and we have received others from helpful
visitors to our web site. The list likely is not complete. Please
let us know if you can add to it. The more recent ones have links to their
boxscores and play-by-play descriptions.
