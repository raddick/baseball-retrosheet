<p>
<u>July 20, 1947, St. Louis at Brooklyn</u>: In the top of the ninth with the
Cards ahead 2-0, Ron Northey hits a high fly deep to the wall in right center.
The Dodgers CF Pete Reiser leaps but can't catch it, and after a couple of seconds
the ball drops back on the field where RF Dixie Walker fields it, relays it to 2B
Eddie Stanky, whose throw nails Northey at home. The first base umpire (there was
a three man crew in those days) immediately ruled the ball in play, but the other
base umpire signaled to Northey that it was a homer, so Northey slowed up. He was
ruled out at the plate, and the Cards protested the game saying Northey had been
deceived by the umpire and would have scored if he had not slowed down.
<p>
In the bottom of the ninth, the Dodgers scored three runs to win the game before
the protest by a 3-2 score. NL President Ford Frick, who later became Commissioner,
upheld the protest and awarded Northey a home run. However, he did not order the game
resumed in the top of the ninth with the Cards ahead 3-0, which would have been the
normal procedure called for by the rules. He allowed the Dodgers' runs to stand and
ruled the game a 3-3 tie. (We can only speculate on what he would have decided if
the Dodgers had hit a homer in the bottom of the ninth and scored four runs.) All
the records counted except for the pitchers' win and loss. The game was replayed
in its entirety as part of a doubleheader on August 18 that the Dodgers won.
<p>
Retrosheet's Dave Smith wrote an article about this game, which has more details,
in the Society for American Baseball Research publication <i>The Baseball Research
Journal</i>, Number 33, (2004).


