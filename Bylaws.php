<p class="MsoNormal" align="center" style="text-align: center"><b>
<span style="font-size: 10.0pt; font-family: Arial">GENERAL PROVISIONS</span></b></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><u><span style="font-size: 10.0pt; font-family: Arial">
Section 10.1</span></u><span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
Contracts</span></p>
