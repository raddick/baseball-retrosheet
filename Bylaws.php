<p class="MsoNormal" align="center" style="text-align: center"><b><u>
<span style="font-size: 10.0pt; font-family: Arial">ARTICLE III</span></u></b></p>
<p class="MsoNormal" align="center" style="text-align: center"><b>
<span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></b></p>
<p class="MsoNormal" align="center" style="text-align: center"><b>
<span style="font-size: 10.0pt; font-family: Arial">PURPOSES</span></b></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><u><span style="font-size: 10.0pt; font-family: Arial">
Section 3.1</span></u><span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
Primary Purposes</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; The Corporation does not contemplate pecuniary gain or profit to its 
members.&nbsp; The primary purposes and essence of the Corporation are exclusively 
charitable, educational and non-profit as defined under Section 501 (c)(3) of 
the Internal revenue Service Code of 1986, as amended and the regulation 
thereunder.</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><u><span style="font-size: 10.0pt; font-family: Arial">
Section 3.2</span></u><span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
Specific Purposes</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; More specifically, the purposes for which this Corporation is formed 
are to collect, organize, computerize and disseminate play by play accounts of 
Major League Baseball games. The dissemination of data will be to individuals 
interested in baseball as well as to archives and libraries such as the National 
Baseball Hall of Fame Library in Cooperstown, New York.</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal" align="center" style="text-align: center"><b><u>
<span style="font-size: 10.0pt; font-family: Arial">ARTICLE IV</span></u></b></p>
<p class="MsoNormal" align="center" style="text-align: center"><b>
<span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></b></p>
<p class="MsoNormal" align="center" style="text-align: center"><b>
<span style="font-size: 10.0pt; font-family: Arial">MEMBERSHIP</span></b></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; There will be no formal categories of membership in the 
Corporation.&nbsp; Volunteers who donate their time and other friends of the 
Corporation will have access to RetroList, the e-mail list which is used to 
exchange information among RetroFriends.</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal" align="center" style="text-align: center"><b><u>
<span style="font-size: 10.0pt; font-family: Arial">ARTICLE V</span></u></b></p>
<p class="MsoNormal" align="center" style="text-align: center"><b>
<span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></b></p>
<p class="MsoNormal" align="center" style="text-align: center"><b>
<span style="font-size: 10.0pt; font-family: Arial">BOARD OF DIRECTORS</span></b></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><u><span style="font-size: 10.0pt; font-family: Arial">
Section 5.1</span></u><span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
Management of Corporation</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; The business of the Corporation shall be managed by its Board of 
Directors which may exercise all such powers of the Corporation and do all such 
lawful acts and things that (A) are consistent with the purposes of the 
Corporation, and (B) are not by statute or by the Certificate of Incorporation 
or by these By-Laws directed or required to be exercised or done by the Members, 
or (C) would be inconsistent with Section 505 (c)(3) of the Internal revenue 
Code of 1954, as amended (or any corresponding provisions of any future United 
States Internal Revenue Law or Regulations thereunder).</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><u><span style="font-size: 10.0pt; font-family: Arial">
Section 5.2</span></u><span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
Number of Directors</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; The initial number of Directors which shall constitute the Board of 
Directors shall be five (5), and thereafter shall never be less than two (2).</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><u><span style="font-size: 10.0pt; font-family: Arial">
Section 5.3</span></u><span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
Length of Term</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; After the establishment of the initial Board of Directors, the term 
of office for each Director shall be three years.&nbsp; </span>
<span style="font-size: 10.0pt; font-family: Arial">Officers and directors shall 
take office at the end of the annual meeting.</span><span style="font-size: 10.0pt; font-family: Arial"> 
For the initial Board, two Directors shall be appointed for a three (3) year 
term, two for a two (2) year term and one for a one (1) year term.</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><u><span style="font-size: 10.0pt; font-family: Arial">
Section 5.4</span></u><span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
Choice of Directors</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoBodyText">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; After the establishment of the initial Board 
of Directors, vacancies and newly created directorships resulting from any 
increase in the authorized number of Directors may be filled by the Directors 
then in office, though less than a quorum, or by a sole remaining Director. 
Candidates for the Board of Directors will be chosen by a Nominating Committee 
composed of two Directors.&nbsp; These two Directors will be those with the longest 
remaining terms of office and will not include the Chair of the Board.&nbsp; In case 
there are more than two Directors whose terms are equal, the Chair of the Board 
shall choose between the qualified candidates.&nbsp; It is expected that usually one 
candidate will be proposed for each vacant position.</p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><u><span style="font-size: 10.0pt; font-family: Arial">
Section 5.5</span></u><span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
Regular Meetings</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Regular Meetings of the Board of Directors shall be held annually during the month of June. This meeting will be conducted in a manner agreed to in advance by the members of the board
. It is not expected that the board will gather in one location to meet.</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><u><span style="font-size: 10.0pt; font-family: Arial">
Section 5.6</span></u><span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
Quorum</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; A quorum for Regular Meetings of the Board of Directors shall be a 
majority of the Board.&nbsp; Only Board members may make resolutions or introduce 
business at meetings of the Board. &nbsp;Others in attendance at the Board meeting 
may be permitted to speak at the discretion of the Board.</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><u><span style="font-size: 10.0pt; font-family: Arial">
Section 5.7</span></u><span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
Special Meetings</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Special meetings of the Board of Directors may be called by the 
Chair of the Board or upon written request of a majority of the Directors. 
The Chair shall fix a reasonable time for the meeting, provided that the 
time shall be at least ten (10) days after the meeting is requested. It is 
not expected that the board will gather in one location to meet.
</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><u><span style="font-size: 10.0pt; font-family: Arial">
Section 5.8</span></u><span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
Manner of Acting</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; The act of a majority of the Directors at a regular or special 
meeting at which a quorum is present shall be the act of the Board of 
Directors.&nbsp; Unless otherwise restricted by the Certificate of Incorporation or 
these By-Laws, any action required or permitted to be taken at any meeting of 
the Board of Directors may be taken without a meeting, if all members of the 
Board consent thereto in writing, and the writing or writings are filed with the 
minutes of proceedings of the Board.&nbsp; Such consent shall have the same force and 
effect as a vote of the Directors assembled in a meeting.&nbsp; For the purpose of 
this section, a vote submitted by e-mail from the Director’s own e-mail address 
shall be the same as a paper document.</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><u><span style="font-size: 10.0pt; font-family: Arial">
Section 5.9</span></u><span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
Compensation</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Directors shall not receive any salaries for their services nor 
compensation for attendance at meetings of the Board.</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><u><span style="font-size: 10.0pt; font-family: Arial">
Section 5.10</span></u><span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
Removal of Directors</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Any Director may be removed from the Board for cause of misfeasance 
or malfeasance at any time by a vote of two thirds of the other Directors.&nbsp; The 
reasons for proposing the removal of a Director for cause must be listed in the 
resolution calling for removal, a resolution which can only be introduced by 
another member of the Board.&nbsp; Written notification of the proposed removal 
resolution, including the resolution itself, must be sent to all members of the 
Board at least ten (10) days in advance of the meeting at which the vote on 
removal is to be taken.&nbsp; This notification must be by certified mail, return 
receipt requested.</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><u><span style="font-size: 10.0pt; font-family: Arial">
Section 5.11</span></u><span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
Conflicts of Interest</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; The Board of Directors shall develop a conflict of interest policy 
relating to conduct of members of the Board.&nbsp; Any Board member failing to comply 
with this policy shall be subject to removal.</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><u><span style="font-size: 10.0pt; font-family: Arial">
Section 5.12</span></u><span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
Resignations</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Any Director may resign at any time by giving written notice to the 
other members of the Board.&nbsp; Such resignation shall take effect on the date of 
the receipt of such notice or at any later time specified therein; and unless 
otherwise specified therein, the acceptance of such resignation shall not be 
necessary to make it effective.</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal" align="center" style="text-align: center"><b><u>
<span style="font-size: 10.0pt; font-family: Arial">ARTICLE VI</span></u></b></p>
<p class="MsoNormal" align="center" style="text-align: center"><b>
<span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></b></p>
<p class="MsoNormal" align="center" style="text-align: center"><b>
<span style="font-size: 10.0pt; font-family: Arial">OFFICERS</span></b></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><u><span style="font-size: 10.0pt; font-family: Arial">
Section 6.1</span></u><span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
Officers</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; The officers of the Corporation shall be chosen by the Board of 
Directors and shall include a President, a Vice-President, a Secretary, and a 
Treasurer, all of whom are members of the Board of Directors.&nbsp; These officers 
shall be selected by the Board.&nbsp; It is expected that the Chair of the Board of 
Directors shall also occupy the office of President.&nbsp; It is expected that the 
offices of Vice-President and Treasurer shall be occupied by one person, who is 
not also the President and Chair of the Board.</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><u><span style="font-size: 10.0pt; font-family: Arial">
Section 6.2</span></u><span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
Other Officers</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; The Board of Directors may appoint such other officers as it deems 
advisable, each of whom shall have the title, term of office, duties and 
authority as designated by the Board of Directors.</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal" style="text-indent: -1.5in; margin-left: 1.5in"><u>
<span style="font-size: 10.0pt; font-family: Arial">Section 6.3</span></u><span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
Resignations</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Any officer may resign at any time by giving written notice to the 
other members of the Board.&nbsp; Unless otherwise specified in this notice, a 
resignation will take effect upon receipt of the notice by the Board.</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><u><span style="font-size: 10.0pt; font-family: Arial">
Section 6.4</span></u><span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
Chair of the Board of Directors and President</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; It is expected that these two positions will be filled by one person 
who shall be the chief executive officer of the Corporation.&nbsp; Subject to the 
direction and control of the Board of Directors, this individual shall have 
three major areas of responsibility:</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal" style="text-indent: -1.0in; margin-left: 1.0in">
<span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp; A.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; In 
the capacity of Chair of the Board, this person shall chair meetings of the 
Board of Directors and shall have responsibility for preparation of the agenda 
for each meeting of the Board.&nbsp; In setting the agenda for the annual meeting, 
the Chair will issue a request for items to be placed on the agenda by notice on 
the RetroList at least 30 days prior to the annual meeting. The Chair shall 
conduct those meetings according to Robert’s Rules of Order.</span></p>
<p class="MsoNormal" style="text-indent: -1.0in; margin-left: 1.0in">
<span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal" style="text-indent: -1.0in; margin-left: 1.0in">
<span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp; B. &nbsp;&nbsp;&nbsp;&nbsp;&nbsp; In 
the capacity of President, this person shall be in charge of the business 
operations and affairs of the Corporation.&nbsp; Responsibilities include executing 
resolutions and directives of the Board, except in those instances in which 
responsibility for a specific action is assigned to another individual by the 
Board.&nbsp; In the absence of this specific assignment of authority by the Board, 
the President is the only individual who may represent the Corporation in legal 
or financial matters, subject to the functions of the other officers, listed 
below.&nbsp; The President shall report to the Board at each annual meeting all 
actions undertaken on behalf of the organization as authorized by the Board.</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal" style="text-indent: -1.0in; margin-left: 1.0in">
<span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp; C.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; In 
the capacity of President, this person shall take all appropriate actions to 
protect the assets of the Corporation.&nbsp; Specific examples of such protection are 
to be determined by the Board.</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><u><span style="font-size: 10.0pt; font-family: Arial">
Section 6.5</span></u><span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
Vice-President and Treasurer</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; It is expected that these two positions will be filled by one 
person.&nbsp; This individual shall have two major areas of responsibility:</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal" style="text-indent: -1.0in; margin-left: 1.0in">
