<p class="MsoNormal" align="center" style="text-align: center"><b>
<span style="font-size: 10.0pt; font-family: Arial">DISSOLUTION OF THE 
CORPORATION</span></b></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; A resolution to dissolve the Corporation may be made by any Director 
and requires the approval of two thirds of the Board.&nbsp; Upon approval of a 
dissolution resolution, the distribution of the Corporation assets will be the 
responsibility of the Board of Directors.&nbsp; If possible, assets will be 
distributed in one of the following two ways:</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal" style="text-indent: -.5in; margin-left: .5in">
<span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp; 1.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; to a 
charitable organization.</span></p>
<p class="MsoNormal" style="text-indent: -.5in; margin-left: .5in">
<span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp; 2.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; to 
another qualified not-for-profit corporation.</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; In the event that it is not possible to distribute assets in one of 
these two ways, then they may be distributed in any other manner approved by the 
Board.&nbsp; Distribution of assets must be in accordance with the laws of the State 
of Delaware.</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
