Section 13.1</span></u><span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
Ratification/Severalability</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
