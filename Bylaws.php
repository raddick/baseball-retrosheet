the capacity of Vice-President this person shall assist the President in the 
discharge of his or her duties as requested by the President and shall perform 
other duties as assigned by the Board.&nbsp; In the absence of the President or in 
the event of the President's inability or refusal to act, as certified by the 
Board, the Vice-President shall perform the duties of President and Chair of the 
Board of Directors and, when so acting, shall have all the powers and authority 
of the President.</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal" style="text-indent: -1.0in; margin-left: 1.0in">
<span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp; B.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; In 
the capacity of Treasurer this person shall have the primary authority to 
disburse funds as authorized by the Board of Directors.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; This individual 
shall also be the principal accounting and financial officer of the 
Corporation.&nbsp; This responsibility includes the maintenance of adequate records 
and books of account for the Corporation, the payment of all bills approved by 
the Board, and the preparation and filing of all required tax and fiscal 
statements, including a complete financial statement.</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><u><span style="font-size: 10.0pt; font-family: Arial">
Section 6.6</span></u><span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
Secretary</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; This person will keep and publish minutes of all Board meetings, 
maintain an up-to-date copy of the By-Laws of the Corporation, maintain an 
accurate history of all changes to those By-Laws, maintain an up-to-date copy of 
the Policy Manual of the Corporation, and maintain an accurate history of all 
changes to that Policy Manual.</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><u><span style="font-size: 10.0pt; font-family: Arial">
Section 6.7</span></u><span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
Order of Succession</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; In the absence of one or more officers or in one or more officer's 
inability or refusal to act, as certified by the Board, the following is the 
order of succession to higher office on the incorporation's Board of Directors:</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; President/Chair</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Vice-President/Treasurer</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Secretary</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Board member with the longest term remaining</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Board member</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">Any 
board member who takes the place of a higher officer shall be considered as 
acting in that position temporarily.</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><u><span style="font-size: 10.0pt; font-family: Arial">
Section 6.8</span></u><span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
Removal of Officers</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Any Officer may be removed for cause of misfeasance or malfeasance 
at any time by a vote of two thirds of the remaining Directors.&nbsp; The reasons for 
proposing the removal of an officer for cause must be listed in the resolution 
calling for removal, a resolution which can only be introduced by another member 
of the Board.&nbsp; Written notification of the proposed removal resolution, 
including the resolution itself, must be sent to all members of the Board at 
least ten (10) days in advance of the meeting at which the vote on removal is to 
be taken.&nbsp; This notification must be by certified mail with return receipt 
requested.</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><u><span style="font-size: 10.0pt; font-family: Arial">
Section 6.9</span></u><span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
Disbursement Limitation</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; No financial transaction involving more than five hundred dollars 
($500.00) shall be undertaken without the explicit prior approval of the Board.</span></p>
