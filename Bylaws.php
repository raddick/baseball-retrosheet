<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Checks, drafts or other orders for the payment of money issued in 
the name of the Corporation shall normally by signed by the Treasurer.&nbsp; If the 
Treasurer is unable or unwilling to sign a check for an expense that has been 
authorized by the Board, then the President shall sign the check.</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><u><span style="font-size: 10.0pt; font-family: Arial">
Section 10.3</span></u><span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
Deposits</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; All funds of the Corporation shall be deposited to the credit of the 
Corporation in such banks, trust companies or other depositories as selected by 
the President, subject to the approval of the Board.&nbsp; All accounts are to be 
structured so that the President and Treasurer each have access.</span></p>
<p class="MsoNormal"><span style="font-size: 10.0pt; font-family: Arial">&nbsp;</span></p>
<p class="MsoNormal"><u><span style="font-size: 10.0pt; font-family: Arial">
Section 10.4</span></u><span style="font-size: 10.0pt; font-family: Arial">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
Gifts</span></p>
