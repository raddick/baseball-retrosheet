<H2>Resumed Protested Game: 7/2/1934</H2>
<p>
With the Cubs up 4-1 in the bottom of the 7th, bases loaded and one out, Chuck Klein popped in front of the plate.
The catcher let it fall then threw wildly to home. Umpire Bill Klem called no infield fly. The Cards protested, and
the Cubs scored 2 more runs and lead 7-1, winning 7-4. The protest was upheld (first time Klem was proven wrong
said one story) and the game resumed on July 31 in the 7th with 2 out and the score 5-1, with runners on 2nd and 3rd.
The Cubs failed to score but added 2 in the 8th and Warneke shut the Cards down to give the Cubs a 7-1 win.
