<H2>Resumed Protested Game: 8/17/1947</H2>
<p>
We are not sure if the Dodgers actually protested the game, but the
game ended up being treated as if it were a resumed protested game. We
are continuing to research it.
<p>
The game was the second game of a Dodgers at Phillies doubleheader. At
the time Pennsylvania had a curfew on Sunday games saying they had to
end no later than 6:59 PM. The Phillies had won the opener 4-0, and the
second game was tied 4-4 after six innings as the curfew neared. The
Dodgers scored a run in the top of the seventh, which caused the Phillies
to adopt delaying tactics hoping the inning would not be completed and
the game would revert back to a six inning tie. At that point, the game
turned into a travesty.
<p>
The Phillies changed pitchers to eat up some time. The Dodgers then
countered by having the runners on first and second trot toward
the next bases hoping the Phillies would put them out, but the Phils
permitted a double steal. Dodger batters swung at pitches well out of
the strike zone. One of them was during an intentional walk issued after
the double steal, but the home plate umpire did not allow that Dodger
tactic. Finally the inning ended when the Dodger on third tried to "steal"
home by trotting towards the plate and the Phillies decided they
could not allow the run to score.
<p>
In the bottom of the seventh, the Phil's first hitter, Harry Walker, could not find his
bat! That delayed things enough so that the count on him was 2-1 when
the curfew went into effect at 6:59. At that point, the game was considered
to be a six inning 4-4 tie.
<p>
The next day Ford Frick, National League President and later Commissioner,
ruled that the game must be resumed (on September 25 when the Dodgers were
next in Philadelphia) with Walker at bat with a 2-1 count and none out in the bottom of the
seventh. He based his ruling on the umpire's report and possibly
on the Dodgers protest if they made one. His official statement said "it was
a farcical exhibition which was a disgrace to baseball and a complete travesty
of all the rules of sportsmanship."
<p>The Dodgers ended up winning the game 7-5 when it was completed on September
25 before the regularly scheduled game.

