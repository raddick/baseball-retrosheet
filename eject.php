<H2>Ejections</H2>
<P>Ejections are a common feature of baseball with some confrontations being especially memorable.  The first systematic compilation<br>
of ejection details was that of Doug Pappas. David Vincent wrote a brief history of ejections in a paper that is on the research page<br>
of the Retrosheet website: (<a href="https://www.retrosheet.org/Research/VincentD/EjectionsHistory.pdf">Ejection History</a>).  The
ejection database is now maintained by Retrosheet.<br>

<P>  David noted that umpires only gained the authority to remove unruly players, coaches, and managers in 1889.  Prior to that time,<br>
the only penalties for misbehavior were monetary fines and possibly suspensions.  David Smith analyzed patterns of ejections<br> for SABR50. That presentation is also on the Retrosheet
research page (<a href="https://retrosheet.org/Research/SmithD/EjectionsThroughTheYears.pdf">Ejection Patterns</a>).<br>
<P>As a prelude to the SABR50 paper, the Retrosheet compilation of ejections was expanded, put into standard format and proofed<br>
for accuracy. Rob Wood was instrumental in these enhancements.</P>

<P>Retrosheet now has detailed information on nearly 18,000 ejections and our full data base is available for download here:
<a href="https://www.retrosheet.org/Ejecdata.txt">Ejection Data</a>,.<br>
This chronologiclal list is a standard text file suitable for import into spreadsheets or database programs or for use in programs<br>
written by interested individuals.

<P>Each record in this file has the following fields.<br>
Retrosheet Game id<br>
Date<br>
Game number (blank for single games)<br>
Retrosheet id of individual ejected<br>
Name of individual ejected<br>
Retrosheet abbreviation of the team of the ejected person<br>
Role of the ejected person (P for player, M for manager, C for coach:<br>
Retrosheet id of ejecting umpire <br>
Name of ejecting umpire<br>
Inning of ejection (-1 when inning is unknown)<br>
Brief reason<br>

<P>
There is no doubt that more ejections remain to be discovered. If you find any new information, please send supporting evidence to<br> "webmaster-ATsign-retrosheet.org".
