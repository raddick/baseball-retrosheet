<p><b style='mso-bidi-font-weight:normal'><span style='font-size:13.5pt'>Original
Regular Season Schedules <o:p></o:p></b></p>

<p>These schedule files contain the originally planned schedules for each major
league season from 1877 through 2023. There was no published schedule available
for the 1876 season. Schedules were obtained from different sources, such as
The Sporting News Baseball Guide and the New York Times. Occasionally there
were disagreements between these sources which were then subjected to careful
research. The basic format is parallel to that of the Retrosheet Game Logs,
with fewer fields. The file format is described below. </p>

<p>Data collection was done by Paul Rivard and Walter and Kathy LeConte.
Proofing of the schedules was done by Dave Lamoureaux, Jim Herdman, Cliff Blau,
Sheldon Miller, Jon Rickert, Gary Pearce, Jim Wohlenhaus, Dave Smith and Paul
Rivard. Frank Vaccaro has donated his research on postponed games, which has
been incorporated into the schedules. </p>

<p>The data on postponements were compiled by David Vincent after Dave Smith
systematically identified the games that were not played as scheduled. 
The makeup dates were determined and added to the original date,thus
producing the final set of files that are presented here. </p>

<p>Walter LeConte wrote an analysis of the 1995 season schedules, found <a
href="http://www.retrosheet.org/Research/LeConteW/1995schedule.pdf">here</a>. </p>

<p><b>Note that the 2020 ZIP archive has two schedules within it.  The first,
named "2020orig.txt" has the schedule as it was planned before the pandemic. 
The seoncd file, "2020rev.txt" is the revised version with the shortened
schedule (60 games per team) released by MLB in early July of 2020,</b></p>

<p><b><i>Fields and their descriptions</i></b><b style='mso-bidi-font-weight:
normal'><i style='mso-bidi-font-style:normal'> <o:p></o:p></i></b></p>

<pre>
Field(s)      Meaning
   1          Date in the form &quot;yyyymmdd&quot;
   2          Number of game:
                     &quot;0&quot; - a single game
                     &quot;1&quot; - the first game of a double header including separate admission doubleheaders
                     &quot;2&quot; - the second game of a double header including separate admission doubleheaders
   3          Day of week(&quot;Sun&quot;,&quot;Mon&quot;,&quot;Tue&quot;,&quot;Wed&quot;,&quot;Thu&quot;,&quot;Fri&quot;,&quot;Sat&quot;)
   4-5        Visiting team and league
   6          Season game number for visiting team
   7-8        Home team and league
   9          Season game number for home team
   10         Day (D), Night (N), Afternoon (A), Evening (E for twinight)
   11         Postponement/cancellation indicator (more detail below)
   12         Date of makeup if played in the form &quot;yyyymmdd&quot; (more detail below)

<b><i>Field 11 explanation</i></b>

This field will contain one or more phrases related to the game if it was
not played as scheduled. If there is more than one phrase, they are separated
by a semi-colon (&quot;;&quot;). There are three possible outcomes for games not played
on the originally scheduled date:
<ul>
<li>The game was played on another date
<li>The game was played on the original date but at another site
<li>The game was not played
<li>This field can contain one or more standard phrases:
<li>No Makeup played
<li>Site Change
<li>Played at XXX (where XXX is team/city code)
<li>Rain
<li>Cold
<li>Snow
<li>Threatening weather
<li>Inclement weather
<li>Hurricane XXX (where XXX is the name of the hurricane)
<li>Wet grounds
<li>Darkness
<li>Power failure
<li>Lighting failure
<li>Schedule change
<li>World War I
<li>9/11 attack
<li>Stadium ... (damage or others words follow)
<li>Strike
<li>Sunday game stopped
<li>Sunday curfew
<li>Accommodate large crowd
<li>XXX folded (where XXX is team code)
<li>XXX played as YYY (where XXX &amp; YYY are team codes)
<li>XXX home team (where XXX is team code; used for games at alternate site)
<li>Death of ...
<li>Funeral of ...
<li>Unknown reason
</ul>

<b><i>Field 12 explanation</i></b>

This field will contain a makeup date if the postponed game was played at
another time or place. If an attempt was known to have been made on a date but
postponed again, that date will be listed. In that case, there will be a second
date for the date when the game was actually played, in this form: &quot;20150428;
20150528&quot; For the note about a team folding, the team code is one of the
standard Retrosheet team IDs. The same is true for the team played as note.
Often, the two of these are combined in one field. The teams that folded are:

   Team/Lg       Last Game
   SR1 NL        9/10/1879
   TRN NL        9/20/1879
   ALT UA        5/31/1884 [moved to KCU 6/07]
   CHU UA        9/18/1884 [moved to MLU 9/27]
   PHU UA        8/07/1884 [moved to WIL 8/18 to 9/12; moved to SPU 9/27]
   WS7 AA        8/02/1884 [moved to RIC 8/05]
   BR4 AA        8/25/1890 [moved to BL3 8/27]
   CN3 AA        8/16/1891 [moved to ML3 8/18]
</pre>

