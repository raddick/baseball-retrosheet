<p class=MsoNormal><o:p>&nbsp;</o:p></p>

<p class=MsoNormal>At the 2005 meeting, I announced that we had reached the 1
million mark in visitors to our site.<span style='mso-spacerun:yes'> 
</span>That count began in January of 2002.<span style='mso-spacerun:yes'> 
</span>Since the 2005 meeting, which was in August, we have had another 500,000
hits, which is a remarkable increase.<span style='mso-spacerun:yes'> 
</span>Our average is now around 2000 per day with spikes whenever we are
mentioned in the New York Times, or in an ESPN column or a newspaper in a major
league city.</p>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

<p class=MsoNormal>As required by Board resolution, I ask for donations to
support Retrosheet’s costs.<span style='mso-spacerun:yes'>  </span>Our largest
fixed cost is the monthly server fee of around $215.<span
style='mso-spacerun:yes'>  </span>In addition, we hope to be buying some more
microfilm rolls of official daily records from the Hall of Fame</p>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

<p class=MsoNormal>Volunteer Opportunities (under New Business)</p>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

<p class=MsoNormal>Our largest need continues to be for help in transcribing
daily records from microfilm to digital form, either spreadsheet or text file.
Either format is fine and is the choice of the volunteer.<span
style='mso-spacerun:yes'>  </span><span class=GramE>Having this information in
digital form with help with our proofing activities as well as in making the
