<H2><U>Retrosheet Scoring System History</U></H2>
<p><Font size=4>
Project Scoresheet began collecting play-by-play accounts of all major
league games with the 1984 season. It was not hard to see that traditional
baseball scoresheets, the type that are usually found in game programs sold
at ballparks in score keeping books, would not be well suited for creating
a computer database of play-by-play accounts of games. A better type of
scoresheet and score keeping method was needed. When Retrosheet was formed,
continuing to use the Project Scoresheet system was the best choice for
several reasons.

This page contains a
brief history of the development of the method giving credit to some of
those who played key roles. Any omissions are unintentional and likely
due to poor memories or lack of knowledge on our part.
<p>
If you are not familiar with the method, what follows may not make that
much sense. You can <A href="ex-sheet.htm">
look at an example scoresheet</A> and read a brief, far from comprehensive,
description of the system.
<p>
The key features of scoring system were developed by Craig Wright, baseball 
author and professional sabrmetrician(!). He invented the now-familiar 
three-line scoring form and the notion of recording the game chronologically 
instead of grouping all a player's actions together. While there are other 
successful computer-based input scoring schemes, Craig's is notable for 
being very usable with paper-and-pencil as well as computer friendly.
<p>
While several Project Scoresheet volunteers worked on improving the system, 
the person whose contributions are most apparent is Gary Gillette. Gary 
fleshed out all the details so the scorers could handle whatever cropped up 
in games, and maintained the clear and precise instructions that the 
scorers received each year.
<p>
In 1986, the hit location system was introduced to the Project, developed 
primarily by Gary. One important feature of the method is that one can use
it pretty well even when scoring games over the radio. This was a must for the 
project, since getting the games covered was sometimes quite a challenge, and 
it was not always possible to count on someone attending or getting it on TV.
<p>
The pitch-by-pitch system (not shown in the example scoresheet) was originally
developed by Scott Segrin and made  its debut in 1987. Geoff Beckman rewrote
the instructions for that season, and David Nichols redid the scoring form 
around that time.
