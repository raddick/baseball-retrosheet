<H2><U>Retrosheet Organizational Description</U></H2>
<P>Modern baseball analysis has grown increasingly sophisticated in the past 
decade and a number of new statistical categories have come into common usage 
among announcers and writers. All of these new methods require detailed play by 
play data for current games and several statistical organizations actively 
collect this information. Many fans would like to compare modern player 
performances to those from earlier this century but have been unable to do so, 
since play by play accounts have never been collected or organized in a 
systematic way. Therefore, a new organization, called Retrosheet, was founded in 
1989 for the purpose of computerizing play by play accounts of as many pre-1984 
major league games as possible (data for games since 1984 have been collected by 
Project Scoresheet/Baseball Workshop and other organizations). 
<P>Retrosheet's work has three distinct aspects. First is the collection of the 
game accounts, which have been obtained from several sources. The bulk of the 
collection has come from the major league teams, who have allowed us to make 
copies of their play-by-play accounts. Several sportswriters (most of them 
retired) have allowed copies to be made of their daily scorebooks. In addition, 
individual fans have donated copies of games they scored at the park or on their 
own scoresheets at home. The second activity is the translation of these 
accounts to a unified, modern system which is essential since there are an 
extraordinary variety of scoring systems which have been used. The final 
activity is the entry of the translated accounts into the computer. 
<P>Retrosheet has been very successful in the collection of game accounts with 
more than 100,000 currently in hand. A group of some 100 volunteers is actively 
involved in the translation work, computer entry and proofing the results. However, the task ahead 
is enormous and we are always looking for more volunteers; any offers of help 
are greatly appreciated. Baseball fans interested in this historical effort are 
invited to volunteer their assistance in the translation and inputting efforts 
as well as to make available copies of game accounts they might have. 
<P>The ultimate objective of Retrosheet is to make this basic play by play 
information publicly available for all interested researchers. Distribution of 
play-by-play data will be done through this web site. Choose &quot;Play-by-play files&quot; 
under Data Downloads for more information on the current years available.

