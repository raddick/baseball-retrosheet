<h3>09/03/1939 - New York @ Boston AL (2nd game)</h3>
<a href=https://www.retrosheet.org/boxesetc/1939/B09032BOS1939.htm>Boxscore</a>

<p>
From <i>The New York Times</i> (09/04/1939 issue) and other sources:<br>
<p>
- In the top half of the 8th inning of the second game of a doubleheader,
the Yankees loaded the bases with two singles and a walk. A force out at home,
a throwing error while attempting to complete the double play and a double
left the Yankees ahead by two runs with runners on 2nd and 3rd and one out.
However to make it a complete game, Boston would have to complete their half
of the inning before a 6:30 Sunday curfew ended the game. The Boston pitcher
proceeded to give the next batter, Babe Dahlgren, an intentional walk, but
Dahlgren, swung at it and the runner from 3rd ran home to be tagged out.
The same thing happened on the next pitch. The Boston manager ran from the
dugout and protested the actions. The Fenway Park fans started throwing
bottles in protest. The game was called by umpire Cal Hubbard in favor of the Yankees.
A few days later, American  league President Will Harridge overturned the forfeit,
so the game went back to the 5-5 tie it was at the end of the seventh inning.
<p>
The game was ordered to be replayed on September 26, but was rained out that
day and never made up because it would not have affected the final standings.
