<h2><P>Transactions</h2>

<P>Our first posting of data about trades and other player transactions contains
data files in fairly "raw" form. We plan eventually to provide access to this information
via Internet screens and links. For the time being, you will have to work with
the data files that you can download here.
<p>
The <a href="readme.txt">read me file</a> describes the layout of the records
and discusses what is and is not in the files and related topics. For your
convenience, this file is included in the transaction data download.
<p>
There are three data files, each organized somewhat differently. They are designed
for further processing and/or searching using tools such as a database program,
spreadsheet program, or your own custom written program. The data files and the
read me file are combined into one "zip" archive file, whose size is about 1 Mb.
<p><a href="tranDB.zip">Download transaction file archive</a>
 

