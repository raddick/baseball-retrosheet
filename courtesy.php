<H2>Courtesy Runners</H2>
<P>Until 1950, a courtesy runner was allowed for a player if that player had 
been injured and at the moment couldn't continue. The original player then 
stayed in the game defensively in the next inning, although sometimes the
injured player did not return. If the replacement runner was already in the
game, we still count this as a courtesy runner. Most often the courtesy 
runner was already in the lineup. If a courtesy runner scored, he got the
credit for the run scored. We also have four examples of courtesy fielders 
listed here. This list is due mainly to the efforts of Dave Vincent with enhancements
by Rob Wood. If you know of any other instances, please e-mail webmaster--ATsign--retrosheet.org

<P>Practices in the early years of major league play were often quite different 
from those in the early 20th Century. For more information see <A 
href="https://www.retrosheet.org/earlycrt.htm">Early Practices</A>.
 
<P><B><U>Mid-Play Runners</U></B>:<BR>We have found two cases when a player who 
was entitled to advance was physically unable to do so. A pinch runner was 
allowed to enter the game and complete the play. These are not courtesy runners 
because the replaced player was known to be unable to later come back into the game. The relevant 
rule is 5.10(c)(1) which allows for the replacement of an injured runner in a 
dead ball situation before play resumes. We list them here below the courtesy 
runners list because of the unusual circumstances for a pinch runner. If you 
know of any others, please let us know. 
