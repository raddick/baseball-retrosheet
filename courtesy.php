<BR><BR>Here are the known instances of this event from our files: <U>
<P></P>8/1/1877 (St. Louis at Louisville)</U> - In the fifth inning, Art Croft 
ran for pitcher Joe Blong. No reason is known but both players are listed in the 
lineup as playing the entire game according to the <I>The Louisville 
Courier-Journal.</I>
<P></P>
<P><U>8/28/1877 (Louisville at Boston NL)</U> - Jumbo Latham was hurt playing 
first base for Louisville. Al Nichols finished an inning for Latham but Jumbo 
batted in the next half inning and singled. Leftfielder George Hall ran for 
Latham. When Hall's turn at bat came around centerfielder Bill Crowley replaced 
Hall as the courtesy runner. After all this Latham returned to the field in the 
next half inning.</P>

<P><U>8/29/1882 (Baltimore at St. Louis AA)</U> - Jumbo McGinnis grounded to the Baltimore pitcher
Doc Landis but Ed Brown was running for him from behind the plate. On a throwing error, Brown circled
the bases with the winning run.

<p><u>6/18/1883 (New York at St. Louis AA)</u> - George Strief of the St. Louis club
injured his leg in the fourth inning. In the fourth, Strief made a hit and
limped to 1B where Tony Mullane waited. Mullane took over as a courtesy
runner once Strief reached 1B and Mullane ran to 3B. In the bottom of the
sixth inning, Strief hit the ball into an area that was closed off down the
RF line but the ball was considered in play. The enclosed area had the
players' bath in it. John Ewing ran the bases for Strief as Chief Roseman
attempted to retrieve the ball. Ewing scored on the play for a home run.
Ewing was in the game himself and merely ran for Strief on the play. 

<P><U>8/21/1884 (Detroit at Boston NL)</U> - Tom Kearns strained his knee 
running out a hit in the fifth inning. Frank Cox ran for him and was eventually 
forced out at third. Frank Meinke replaced Kearns in the field and homered later 
in the game.

<p><u>5/30/1887 (Cincinnati at Baltimore AA)</u> - In the fourth inning with one out, Jumbo Davis of Baltimore, in the eighth spot in the batting order, singled and stole second base. He tore his pants sliding into the bag at second and was replaced by Oyster Burns. Burns stole third without a wardrobe malfunction, After Matt Kilroy walked, both runners scored on a double by Bill Greenwood. Greenwood stole third and scored on catcher Jim Keenan's throwing error. After Blondie Purcell singled, Burns hit and doubled, eventually scoring a second run in the frame. Davis returned to the field with repaired pants.

<p><u>6/8/1887 Louisville at Baltimore (AA)</u> - In the third inning, Hub Collins tripled and sprained his leg. Chicken Wolf took his place on the base and scored on a hit by Pete Browning."  Wolf then hit in his own spot in the batting order, immediately after Browning, and flew out to RF. At the beginning of the fourth, there was controversy as to who would replace Collins.  Louisville's manager Kick Kelly wanted to substitute Guy Hecker, thus giving Louisville a potential fresh pitching arm, but Baltimore captain Oyster Burns protested this move and as a result, Lave Cross was placed in RF and Wolf moved to left.

<p><u>8/25/1887 (Philadelphia at Cleveland AA)</u> - Henry Larkin was too lame to run the bases so in the fifth inning, Fred Mann ran for his teammate. Cleveland pitcher Mike Morrison picked off Mann but Cub Stricker dropped the ball. Chippy McGarr then drove in Mann with a double. Mann might have run for Larkin the entire game.

<p><u>9/8/1887 (Cincinnati at Baltimore AA)</u> - In the seventh inning, Blondie Purcell of the Baltimores (batting fourth) was hit on the arm by a pitch from Elmer Smith. Oyster Burns (who batted third) ran for Purcell. Burns was forced out at second when Tommy Tucker hit to shortstop Frank Fennelly. Jackie Hayes replaced Purcell in right field when the latter could not return to the field.

<p><u>10/8/1887 (Detroit at Indianapolis NL)</u> - In the bottom of the third frame (at the time the home team often batted first), Detroit's Dan Brouthers singled and was injured stealing second. Deacon White, already in the lineup, ran for Brouthers and scored on Sam Thompson's triple. In the top of the fourth, Charlie Bennett played first base when Brouthers could not continue.

<p><u>4/28/1888 (Cleveland at Baltimore AA)</u> - In the fifth inning, Baltimore's Joe
Sommer was injured running the bases. Shortstop Jack Farrell ran for Sommer,
who returned to the field in the next half-inning.

<P><U>8/6/1888 (Indianapolis at New York NL)</U> - New York's Buck Ewing strained
his injured leg running out a 2-base error in the top of the 5th, and LF James
O'Rourke was used as a courtesy runner for him.  He was put out on a fielder's
choice on the next play.

<P><U>9/19/1888 (New York at Pittsburgh NL)</U> - In the top of the second with
home team Pittsburgh batting first, Jake Beckley hit a Texas Leaguer and
took second when no one covered the bag. He injured his ankle, and the Pittsburgh pitcher Ed Morris ran for him.
Apparently Beckley would have been permitted to stay in the game had he been able to do so, but he was
hurt too badly and had to leave the game.

<P><U>9/22/1888 (Cleveland @ St. Louis AA, game 1)</U> - In the sixth inning, Harry Lyons of the Browns was hit on the hand by Cleveland pitcher Cinders O'Brien. Charlie Comiskey, who was hitting fourth in the lineup, ran for Lyons and eventually scored a run. When Lyons could not continue, Comiskey moved to CF and Jack Boyle replaced Lyons in the sixth spot in the lineup and played first base.

<P><U>6/14/1889 (Kansas City at Brooklyn AA)</U> - Brooklyn's Oyster Burns was 
hurt while running the bases in the eighth inning. Catcher Doc Bushong was 
allowed to run for Burns and scored a run in the inning. Both Burns and Bushong 
remained in the game. 
<P><U>9/2/1889 (Pittsburgh at New York, game 2)</U> - As was a usual practice of 
the day, the home team batted first in this game. In the bottom of the third Ned 
Hanlon, hitting in the leadoff spot, singled with one out. Doggie Miller, 
already in the game in the seventh spot in the lineup, ran for Hanlon. He was 
stranded at third when the inning finished. In the next half inning, Bill 
Sowders replaced Hanlon in the field. <U>
<P>9/5/1889 (Indianapolis at New York)</U> - The visitors were batting in the 
bottom of the fifth when Tim Keefe's pitch hit Ed Andrews in the stomach. Jack 
McGeachey, who was already in the game, ran for Andrews and scored on Jack 
Glasscock's double. Andrews remained in the game.

<P><U>5/1/1891 (Cincinnati at Cleveland NL)</U> - In the bottom of the fourth,
Cleveland's Ralph Johnson was hit by a pitch and limped to the bench for treatment.
Cupid Childs, who was playing second base in the game, ran for him and eventually
scored. Johnson returned to the field in the top of the fifth.

<P><U>5/22/1891 (Boston at Cleveland NL)</U> - In the fifth inning, Cleveland's Jack
Doyle was hit on the left elbow while running the bases. When he could not continue,
Cupid Childs, who was already in the game playing second base, ran for Doyle.
Doyle was unable to continue in the field, so George Davis moved from center field
to third base and Ed Seward replaced Doyle, playing center field. Although the
replaced runner left the game, Childs was in the contest, so is considered a courtesy runner.

<P><U>5/27/1891 (Brooklyn at Cleveland NL)</U> - In the seventh inning, Adonis Terry
walked but twisted his ankle stepping  on the base. Catcher Tom Kinslow ran for him
and Terry returned to the mound in the next inning.

<P><U>6/8/1891 (Cleveland at Brooklyn NL)</U> - In the fourth inning, Jimmy McAleer
was running to third, where the fielder, George Pinkney, swung his arm and hit
McAleer on the back of the head. After a delay, Jerry Denny, already in the lineup,
ran for McAleer. In the next inning, McAleer returned to the field but seemed ill.
He was removed from the game and the Brooklyn doctor who examined him called it a
mild concussion. Ed Seward replaced McAleer in the field.

<P><U>7/3/1891 (Baltimore at Philadelphia AA)</U> - Baltimore's Perry Werden spiked 
Philadelphia's Tommy Corcoran in the first inning. Henry Larkin ran for him. 

<P><U>6/21/1894 (Boston at Washington)</U> - In the third inning, Al Maul of 
Washington, leading off, was hit on the left arm and injured. Deacon McGuire, 
Washington's catcher and cleanup hitter, ran for him. McGuire advanced to second 
on a walk, then was safe trying to steal third when Boston third baseman Billy 
Nash dropped the throw. After a fly out, McGuire scored on a triple. 
Interestingly, he was the next batter due at the time.

<p><u>8/28/1894 (Chicago at Philadelphia NL)</u> - In this game, catcher Jack Clements
was run for by two different courtesy runners. In the third inning,
Clements, in the seventh spot in the lineup, singled. Sam Thompson, batting
fifth and playing right field, ran for Clements and scored. In the bottom of
the seventh inning, Clements was hit by a pitch. Bill Hallman, hitting sixth
and playing second base, ran for Clements. Hallman advanced to third on a
wild pitch but was out on a fielder's choice later in the inning. Clements
caught the entire games for the Phillies, who won, 16 - 6.

<p><u>6/14/1897 (Chicago at Brooklyn NL)</u> - In the bottom of the sixth, Dan Daub
of Brooklyn (batting ninth), led off and was hit by a pitch.  John Grim
(batting eighth) ran for him and scored.  Daub was well enough to continue pitching,
and he singled in the seventh.

<p><u>9/10/1897 (Cleveland at Washington NL)</u> - Cleveland's Patsy Tebeau strained
his side running from first to third on Chief Zimmer's double to right in the
seventh inning. Nig Cuppy, who was not in the game, ran for Tebeau and scored
when port McAllister hit a hard bounder to Cy Swaim. Tebeau remained in the game.

<p><u>5/25/1899 (Brooklyn at St. Louis NL)</u> - In the bottom of the ninth
inning, Patsy Tebeau was hit by a pitch and injured. First baseman
Jack O'Connor was sent to first to run for his teammate. O'Connor
was already in the lineup. Since the game ended after that half
inning, Tebeau did not return to the field.

<P><U>9/23/1901 (Brooklyn at Cincinnati)</U> - Sam Crawford tripled off of Jay 
Hughes in the eighth inning, but "pulled up lame" at third. According to the 
account in the <I>Cincinnati Enquirer</I> the next day, "the Brooklyns let 
Heinie Peitz score a run for him and then permitted the Nebraskan [Crawford, for 
anyone who doesn't know the whereabouts of Wahoo] to return and finish the 
game."

<p><u>9/27/1901 (Cleveland at Philadelphia)</u> -  Ollie Pickering batted first for
Cleveland with Zaza Harvey second.  After Pickering was retired in the top
of the fifth, Harvey was beaned by a pitch. Pickering ran for Harvey and
scored later in the inning. Harvey was hurt badly enough to be taken out of
the game, as Bob Wood replaced him in LF in the next inning.

<p><u>5/12/1902 (Chicago at Detroit)</u> - In the bottom of the sixth inning,
Doc Casey ran from 1B to 3B on Dick Harley's single to RF. Pop Dillon replaced
Casey at 3B and was stranded there. Casey returned to the field in the next inning.

<U><P>6/26/1902 (Giants at Philadelphia)</U> - In the bottom of the eighth, catcher 
Fred Jacklitsch was hit "behind the ear" by a pitch from Roy Evans, and "dropped 
like a shot." Hallman, who was in the game as the third baseman ran for him and 
Jacklitsch returned to his duties in the ninth.

<p><u>9/11/1903 (Philadelphia(AL) at New York(AL)</u> -  In the bottom of the first,
John Ganzel was hit in the head by a pitch from Eddie Plank, and Clark Griffith
ran for him.  Ganzel returned to the game in the top of the second.

<P><U>9/17/1903 (Cleveland at Boston)</U> - Cy Young ran twice in this game for
Charley "Duke" Farrell who was playing his first game coming back from an injury.
In the bottom of the fifth, Farrell singled and Cy ran for him, moving to third
on a single to center and scoring on a triple. In the sixth, Farrell doubled,
and Young once again was permitted by Cleveland to run for him, but did not
advance. Boston won the game 14-3. Young was a capable runner as can be seen
from his two stolen bases two days earlier while hurling a 12-3 victory over
New York.

<P><U>5/30/1904 (Giants at Philadelphia, game 1)</U> - In the top of the sixth of 
