<H2>Suspended Games</H2>
<P>The following games were suspended and completed at a later date,
with some of them resuming in the visiting team's city. The columns "V" and
"H" represent the number of runs scored by the visitors and home
team at the time of the suspension and the column "Out" is the
number of outs recorded before the suspension. Since there are
six outs per inning, an entry of 48 outs means eight innings were
completed at the time of the halt in play. A league indicator of
"IL" means that was an Interleague contest.
<P>
The notes column shows a numeric code indicating the reason for the
suspension. The codes are explained at the bottom of the list. Some
causes were fairly common. Before lights
were installed at the Cub's Wrigley Field, darkness caused games to be
suspended. Curfews also were a typical cause. For a time the AL did
not permit any inning to start after 1 AM, and there were blue laws
in Pennsylvania that prevented Sunday games in Philadelphia and Pittsburgh
from going beyond 7 PM.
<p>
Upheld Protests result in suspended games at the point of the protest.
We show those for which we have sufficient details. There is a
<a href="https://www.retrosheet.org/protests.htm">list of upheld protests</a>
on the site.
<p>
Click on the date for a link to a boxscore and play by play account
when available. However, some of those do not indicate that the game
was suspended, which will be added on the next major update of the
site. 
<p>
Please send any missing suspended games to the webmaster at
"webmaster --Atsign-- retrosheet.org".
