<P>This is a listing of major league no hitters organized by franchise. The list is 
also available <A href="https://www.retrosheet.org/nohit_chrono.htm">chronologically</A>,
 <A href="https://www.retrosheet.org/nohit_pitcher.htm">alphabetically 
by pitcher</A>, and <A href="https://www.retrosheet.org/nohit_umpire.htm">alphabetically by umpire</A>. 
There are a number of no hitters in which multiple pitchers have combined to 
blank the opponents. For these games, the pitchers are listed in the order they 
pitched with the number of innings each pitched in parentheses. Click on the 
date to see the box score and play-by-play, if available.
<p>The franchise listings are shown by the geographical name when the first one
was pitched. For example, all of the Braves no hitters are grouped with Boston NL,
and the Angels start with Los Angeles AL.<br>

