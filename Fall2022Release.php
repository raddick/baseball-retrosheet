<p><b>Retrosheet's Second Semi-Annual Release of 2022</b></p>

<p>Retrosheet is pleased to announce its second semi-annual update of 2022. With the release of event files for all AL/NL games for the 2022, 1921, and 1922 seasons (including some deduced games in the latter two), Retrosheet has now passed the century mark with play-by-play accounts for all AL and NL games played over the past 102 years.</p>

<p>This will be the final Retrosheet release for which Tom Ruane will put together the web pages. Retrosheet and the entire baseball community owe Tom a tremendous debt of gratitude for his extraordinary contributions to the world of baseball statistics and baseball research. Continuing thanks are also due to Dave Smith who has stepped back but continues to provide critical guidance to this project. Finally, I would like to thank Rob Wood, who has stepped forward to help me fill the enormous role left by Dave stepping back. Rob has been a tremendous asset to Retrosheet for many years and his tireless work was critical to this release.</p>

<p><b>What's New in This Release</b></p>

<ul>
<li>2022 season. Special thanks to Ted Turocy and Pete Palmer for their help with the 2022 season. See <a href="https://www.retrosheet.org/boxesetc/2022/Y_2022.htm">[2022 season]</a></li>
<br>
<li>1921 and 1922. Event files, including some deduced files, are now available for all of the 1921 and 1922 seasons. Special thanks to Rob Wood, Cliff Blau, Dick Cramer, Jim Herdman, Dave Smith, and Wayne Townsend. See <a href="https://www.retrosheet.org/game.htm">[Data downloads]</a></li>
<br>
<li>1942 and 1943 Negro League seasons. Special thanks to Alan Cohen, Dean Esgrow, Dave Lamoureaux, and Wes Tervo. Pages associated with <a href=https://www.retrosheet.org/NegroLeagues/NotableNgLgGames.html>notable Negro League games</a> have also been updated. See <a href="https://www.retrosheet.org/NegroLeagues/NgLeagueSeasons.html">[Negro League Seasons]</a></li>
<br>
<li>1914 season. Event files are now available for approximately two-thirds of all major-league games played in 1914. Special thanks to Dave Smith. See <a href="https://www.retrosheet.org/boxesetc/1914/Y_1914.htm">[1914 season]</a></li>
<br>
<li>In conjunction with the release of 1914 event files, Retrosheet has introduced a new record type, presadj, to accommodate pre-1940 rules which gave official scorers discretion to assign run responsibility to pitchers who allowed inherited runners to score. The issue of run responsibility and Retrosheet's treatment of it are described <a href=https://www.retrosheet.org/presadj.htm>here</a>.
<br><br>
The presadj record will allow Retrosheet's records to more closely align with official records prior to the codification of modern scoring rules and eliminate the need to resort to discrepancy entries to deal with these cases.
<br><br>
Retrosheet's software tools, bevent and bgame, have been updated to accommodate this. Our tools have also been updated to accommodate the 'Ohtani rule' introduced in 2022 which allows a player to play both P and DH in the same game, remaining in one position even after being replaced at the other. Special thanks to Dave Smith and Tom Tippett. See <a href="https://www.retrosheet.org/tools.htm">[Software Tools]</a></li>
<br>
<li>Dave Smith has updated the write-up of Retrosheet's scoring system for event files, including the implementation of the new presadj record type. Thanks, Dave. See <a href="https://www.retrosheet.org/eventfile.htm">[Detailed Description of Event Files]</a></li>
<br>
<li>Finally, improvements have been made to the game accounts for several hundred games which were previously released. These improvements include the following.</li>

<blockquote><li>40 games which had been deduced have been promoted to full event files.</li>
<li>Pitch data have also been added to many games, including World Series games for 1908, 1910 - 1923, and 1930 - 1935.</li>
<li>Additional game accounts have been improved through the discovery of new scorecards, new newspaper sources, and a growing number of radio and television accounts that are being made available online.</li>
<li>In addition, Retrosheet has modified its treatment of 43 21st-century games which were played at the home ballpark of the team who was originally scheduled to be the visiting team. Retrosheet now considers the "home team" in these games to be the team in whose home ballpark the game was played even in cases where this results in the "home team" batting first in the game. A full list of such games can be found <a href=https://www.retrosheet.org/HTBF.txt>here</a>.</li>
<br>
<li>The work of improving existing accounts through the discovery of new and better accounts, including radio and television accounts, is continuing. Special thanks to Wayne Townsend, Jay Wigley, Rob Wood, and others. See <a href="https://www.retrosheet.org/game.htm">[Data downloads]</a></li></blockquote>
</ul>

