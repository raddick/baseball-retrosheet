      <H2>Retrosheet Software Tools for Event Files</H2>

<hr>

      The first three program files are 
      "zipped." Most likely you already have software to "unzip" (i.e. extract files 
      from a .zip archive) these files. For example, it is built into Windows XP and later versions of Windows.
      If you do not have this capability, download PKUNZIP.EXE, the fourth program listed 
      below.
<p>
      All of these programs require PC DOS to run. If using Windows, you will have to open
      a DOS window to get a command prompt. Macintosh users may be able to run these
      programs using Windows or DOS emulation software. Another way for Mac users is
      described below. 
      <HR>

      <UL>
        <LI><A href="https://www.retrosheet.org/bevent.zip">BEVENT.EXE</A>: 
        Creates play-by-play data file suitable for import into database 
        programs, spreadsheets, and custom written programs. Enter the DOS 
	command "bevent -h" for more information. [version update: 11/30/2020]
        <LI><A href="https://www.retrosheet.org/box.zip">BOX.EXE</A>: Generates 
        traditional box scores from Retrosheet data files. Enter the DOS command 
        "box -h" for more information. [version update: 11/30/2020]<BR> 
        <LI><A href="https://www.retrosheet.org/bgame.zip">BGAME.EXE</A>: Makes a 
        summary of constant items for each game such as date, day or night, and 
        the score. Enter the DOS command "bgame -h" for more information. 
        [version update: 11/30/2020] <BR>
        <LI><A href="https://www.retrosheet.org/pkunzip.exe">PKUNZIP.EXE</A>: 
        Expands compressed data files from the ".zip" file archive. The DOS 
        command "pkunzip name.zip" will write all the files in the archive 
        name.zip to your hard disk. The DOS command "pkunzip 1967al.zip *.ev*" 
        will extract only the Retrosheet event files (game data) from the 
        archive 1967al.zip and write them on your hard drive. Enter the DOS 
        command "pkunzip -h" for a list of the command options. [last update: 
        2/1/93] <BR></UL>

<hr>
A Macintosh user has let us know how he can get these programs to run. If you have questions about this approach, you can e-mail him at
mesamb1--insert the AT sign--gmail.com.

<p>To run the Retrosheet programs Mac users will need to install Wine onto their computer.  Wine will allow for the mac user to emulate
a windows environment on their computer in order to run the Retrosheet programs.

<p>First, you will need to install <A href="https://winebottler.kronenberg.org">WineBottler</A>.  WineBottler lets you run .exe on your
Mac, but we will not be using that feature for Retrosheet.  Instead when you install WineBottler, you will also install the app called
Wine in an easy to use package. To install WineBottler, go to the website and follow the download instructions provided.

<p>These instructions, at the time of my writing, involve downloading the WineBottler dmg, opening the dmg which will mount a removable
drive, and then dragging the app Wine and WineBottler into your applications folder.

<p>Once you have installed Wine and WineBottler from the WineBottler package to your application folder, you need to hold down control
on your keyboard and click on "Wine" in your application folder.  Choose "Open" from the drop down list.  You may need to enter your
administrator name and password, then when prompted select open.  This is because this is an application not listed on the Apple App
store, and your Mac OS security settings by default will not allow for the application to be opened.  For more information please refer
to <A href="https://support.apple.com/en-us/HT202491">Apple support</A>.

<p>Once you have opened Wine, there WILL NOT be an application on your dock.  Instead, on the menu bar that goes across the top of your
screen, there will be a glass of wine icon.  Click on this icon and select "DOS Prompt" from the options.

<p>Wine creates by default a fake windows environment on your Mac.  By default the folder Wine uses as a C drive is located under: 
Macintosh HD\Users\"Your Admin Username"\Wine Files\drive_c

<p>The Wine DOS Prompt will allow you to navigate inside of this folder using normal DOS commands.  This includes being able to run the
programs bevent.exe, box.exe, and bgame.exe 

<p>The Retrosheet software will need to be placed inside of the drive_c folder in order for them to be accessed by Wine.  For example,
to follow the step by step guide from here you would need to create your folder retro inside the "drive_c" folder(which you located
earlier) to match the step by step guides creation of a folder "C:\retro".  In Wine, a folder located as "drive_c"->"retro"  will be
listed as "C:\retro".  A new folder can either be created by using Mac's Finder, or using the "mkdir retro" command at a DOS Prompt.

<p>Once you have expanded the Retrosheet software somewhere in drive_c you will need to move to the working directory in the manner as
listed in step 4 of the step by step guide.

<p>From here on out, using the Wine Command Prompt is identical to using the DOS Command Prompt as explained in the Retrosheet step by
step guide.


