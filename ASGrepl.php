<h2>Replacement Players in the All-Star Game</h2>
<p>
The 36 players chosen to participate in the first All-Star Game were at Comiskey Park, in uniform and ready to play that
day in 1933. However, in most years since, one or more players was forced, or in some cases chose, to miss the game.
Following is a complete list of those replacements. (<b>Bold print</b> indicates elected starter.) The list was created
by Lyle Spatz, David Smith and David Vincent for the book <i>The Midsummer Classic: The Complete History of Baseball's All-Star Game</i>.
<p>
Please send any additions, such as telling us about an "Unknown", or corrections to the webmaster at
