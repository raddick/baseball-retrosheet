<p class=MsoNormal>Tiebreaker Playoff Games</p>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

<p class=MsoNormal>There have been 16 times that two teams ended the regular
season in a tie and needed an additional game (or games) to determine
postseason participation.<span style='mso-spacerun:yes'>  </span>All of the
American League ones have been a single game, while the National
League staged a best of three series through 1962, after which it switched to
the single game format.<span style='mso-spacerun:yes'>  </span>These
games are truly “playoff” games to be distinguished from the consistent MLB
designation of “postseason” for the current multiple series procedure (Division
Series, League Championship Series, <span class=GramE>World</span> Series).</p>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

<p class=MsoNormal>Since these playoff games are considered part of the regular
season and all individual records from them count in the regular season totals.
(Eddie Mathews passed Ernie Banks for the NL home run title in a 1959 playoff
game and in 2007 Matt Holliday used his playoff game to pass Ryan Howard for
the NL RBI leadership).<span style='mso-spacerun:yes'>  </span>These games
comprise an interesting set and therefore this page was prepared to provide the
details for all of them in one place.

