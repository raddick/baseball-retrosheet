	   <font size="4">Most Wanted Games</font>

     <p>We have game accounts (play-by-play descriptions of the games)
for all seasons from 1974-83, the latest retro-year. For 1947-73, we
have game accounts for more than half of the games played in each of those years. We would love to
complete some or all of those seasons, so we are listing the games for which we do <i>not</i> have
a complete game account. Please e-mail us (<A HREF="mailto:retro@pankin.com">click here to e-mail</a>)
if you have a play-by-play account of one of our most wanted games or know who might have one.
We would even welcome an incomplete play-by-play account for only some of the innings of these games.
