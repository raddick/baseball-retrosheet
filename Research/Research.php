<p align="center"><b><font size="4">Research Papers</font></b></p>
<p>Many researchers have made use of Retrosheet data in their work. Much of 
this work has been made public by presentation at local and national SABR meetings. 
Many of the papers are in the SABR Research Library and some are archived at various 
web sites.<br><br>This section of the Retrosheet site was created to provide 
another outlet for publication of research results. Researchers who have 
original results are invited to submit them to Retrosheet President Tom Thress for possible inclusion 
here. <i>The primary requirement is that Retrosheet data must have been part of the 
work.</i> Work submitted here may also be presented elsewhere. The intention 
is not to be exclusive, but to provide a convenient location for research that 
shares the common thread of using Retrosheet data.</p>

