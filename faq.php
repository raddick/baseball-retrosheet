<a name="New Data Release"></a>
<i>When will additional seasons be posted on the web site?</i>
<p>
12-7-2002
<p>
Retrosheet policy is firm that game information is posted on the web
site only after a detailed process of proofing and comparison to official
totals.  This policy covers the narrative game accounts and box scores as
well as the data files that are our primary form of storage.  Depending
on the number of corrections that need to be made and on the nature of our
raw information, this proofing can take many months for a single season.
It is, therefore, not possible to make promises or predictions concerning
future releases.  Proofing work is always underway and help is always needed
in this area.  See the FAQ on volunteering for more details.
<p>
In addition to the regular season data which comprise the bulk of our
information, Retrosheet also has the data for all post-season games,
beginning with the 1903 World Series.  These games are also being proofed
and are a high priority for release, although once again a specific
timetable is not possible.
